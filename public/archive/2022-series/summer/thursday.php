          <h2>Gold Diggers of 1933 (1933)</h2>
          <h3>Busby Berkeley and Mervyn LeRoy &middot; 97m &middot; 35mm</h3>
          <p>Starting with the knockout performance of “We’re in the Money” that features Ginger Rogers singing in pig latin with a chorus full of dancing coins, <i>Gold Diggers of 1933</i> evokes outrageous Hollywood glamor in its purest form. This second entry in the beloved trio of Busby Berkeley choreographed musicals released by Warner Brothers in 1933 also offers equally brilliant comedic moments from Depression-era stars like Dick Powell and Aline MacMahon.</p>
          <p><i>Preserved by the Library of Congress.</i></p>
