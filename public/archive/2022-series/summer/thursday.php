          <h2>The Last Command (1928)</h2>
          <h3>Josef von Sternberg &middot; 88m &middot; 16mm</h3>
          <p>One of von Sternberg's four silent films, <i>The Last Command</i> is a sophisticated melodrama following Emil Jannings' impassioned performance as a once-great Russian duke who is hired as an extra in a Hollywood film. With its sweeping cinematography and grandiose sets depicting the Russian Revolution, this film is an emotional and satirically bleak outlook on Hollywood and life after Imperial Russia for those who experienced the war at its height.</p>
