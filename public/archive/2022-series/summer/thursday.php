          <h2>The Big Parade (1925)</h2>
          <h3>King Vidor and George Hill &middot; 141m &middot; 35mm</h3>
          <p>Jim (John Gilbert), a lazy rich boy, is persuaded to enlist in the U.S. Army in 1917 to the approval of his businessman father. In France, he befriends two working class soliders and falls in love with a farmer's daughter, Melisande. However, the brutal reality of trench warfare makes its mark on Jim as the Americans march to the front. <i>The Big Parade</i> was written by WWI veteran Laurence Stallings, and was King Vidor's most successful silent film.</p>
          <p><i>Preserved by the Library of Congress.</i></p>
