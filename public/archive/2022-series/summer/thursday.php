          <img src="/images/2022summer/the-naked-city-1948.jpg" alt="The Naked City (1948) still">
          <h2>The Naked City (1948)</h2>
          <h3>Jules Dassin &middot; 96m &middot; 35mm</h3>
          <p>Shot almost entirely on location in New York City, <i>The Naked City</i> depicts a police investigation following the murder of a former model. Jules Dassin’s 1948 noir draws visual influence from the black and white street photography of Ascher Fellig (also known as Weegee), as well as Italian Neorealist aesthetics and production methods. A vivid, double Academy Award winning police procedural that stands alongside the great New York stories.</p>
          <p><i>Preserved by the Library of Congress.</i></p>
