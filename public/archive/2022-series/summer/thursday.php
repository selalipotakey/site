          <h2>Within Our Gates (1919)</h2>
          <h3>Oscar Micheaux &middot; 89m &middot; 35mm</h3>
          <p>The oldest known surviving film by an African-American director, Oscar Micheaux's <i>Within Our Gates</i> was produced following D.W. Griffith’s <i>The Birth of a Nation</i> and the Chicago Race Riot of 1919. The film centers on Sylvia Landry (Evelyn Preer), a Black woman from the South that goes north in search of funding for an overcrowded school. While there, she begins a romance that is shaped by contemporary and past experiences with violent racism.</p>
          <p><i>Preserved by the Library of Congress.</i></p>
