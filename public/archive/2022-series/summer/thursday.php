          <img src="/images/2022summer/make-way-for-tomorrow-1937.jpg" alt="Make Way for Tomorrow (1937) still">
          <h2>Make Way for Tomorrow (1937)</h2>
          <h3>Leo McCarey &middot; 92m &middot; 16mm</h3>
          <p>Released in the same year as Leo McCarey’s screwball classic <i>The Awful Truth</i>, this unforgettable Hollywood gem tells the story of an elderly couple who are forced to separate due to financial reasons. Over the course of ninety-two minutes, the small tragedy of Barkley (Victor Moore) and Lucy (Beulah Bondi) unfolds without a single false note. In the words of critic Dave Kehr, “There are few American films as subtle, moving and bursting with human truth.”</p>
