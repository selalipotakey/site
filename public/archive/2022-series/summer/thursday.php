          <h2>White Heat (1949)</h2>
          <h3>Raoul Walsh &middot; 114m &middot; 16mm</h3>
          <p>James Cagney plays Cody Jarrett, a crime boss with an Oedipus complex, imprisoned after a train job but determined to escape and wrest control of his old gang. This marked a return to gangster roles for Cagney and is arguably his most iconic - especially for the final scene. To distinguish Jarrett from his 1930s characters, Cagney fashioned him into an unhinged, migraine-plagued psychopath and channeled his own father’s alcoholic rages into the part.</p>
