          <img src="/images/2022summer/king-a-filmed-record-montgomery-to-memphis-1970.jpg" alt="King: A Filmed Record... Montgomery to Memphis (1970) still">
          <h2>King: A Filmed Record... Montgomery to Memphis (1970)</h2>
          <h3>Joseph L. Mankiewicz and Sidney Lumet &middot; 185m &middot; 35mm</h3>
          <p>Created using only original primary source material such as unretouched newsreels, <i>King: A Filmed Record...</i> is a powerful video documentary of Martin Luther King Jr. and his nonviolent campaign from the events of the 1955 Montgomery bus boycott until his assassination in 1968. Originally shown in theaters in a "one night only" event, <i>King</i> was turned into a DVD using stored master elements in 2010 and publicly released after being "lost" for 40 years.</p>
        </div>
        
