          <h1>7:00PM Thursday, June 23rd; 8:30PM Friday, June 24th</h1>
          <img src="/images/2022summer/shadow-of-a-doubt-1943.jpg" alt="Shadow of a Doubt (1943) still">
          <h2>Shadow of a Doubt (1943)</h2>
          <h3>Alfred Hitchcock &middot; 108m &middot; 35mm</h3>
          <p>In the words of Hitchcock’s daughter Pat, from the documentary <i>Beyond Doubt</i>: “This was my father’s favorite movie because he loved the thought of bringing menace into a small town.” Shot and set in Santa Rosa, California, <i>Shadow of a Doubt</i>  centers Charlotte "Charlie" Newton and her uncle Charlie, who is visiting town. Portrayed as the paragon of a peaceful, pre-war American city, the town is soon disturbed by Hitchcock’s psychological thrills.</p>
          <p><i>Preserved by the Library of Congress. Funding provided by The Film Foundation and The Hollywood Foreign Press Association.</i></p>
