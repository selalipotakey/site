          <h2>Liquid Sky (1982)</h2>
          <h3>Slava Tsukerman &middot; 118m &middot; Digital</h3>
          <p>Adrian (Paula Sheppard) and her fashion model girlfriend Margaret (Anne Carlisle) navigate the cutthroat, drug-fueled, impeccably styled modelling world of early-80s downtown NYC. But ever since a small UFO landed on their rooftop, Margaret’s sexual partners are struck dead when she brings them to climax — and she must learn to wield her powers wisely. This irreverant, punky, technicolor noir became one of the biggest indie hits of the decade.</p>
