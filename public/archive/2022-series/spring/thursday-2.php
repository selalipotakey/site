          <h1>THURSDAY 2 - Punks Behind the Camera</h1>
          <h3>Programmed by: Eli Timoner</h3>

            <p>It’s much easier to define punk by what it isn’t than what it is. When punk first percolated from working class discontents out of the shadow of arena rock excesses, there was a certain look and sound that punk took: leather jackets, power chords, guttural screams, shaved heads…you know the blueprint. But more than the look or the sound it was an attitude—a rejection of heterodoxy in all forms. Left behind in a world of accelerating capitalism, they saw no future, as many of us do now — so they made a hedonistic world that spat in the faces of squares instead. It was fresh, radical, DIY in its methods. But after those early days punk splintered quickly. New Wave and No Wave took the experimental spotlight of the early 80s, and the widespread popularity of punk subculture emptied it of its original urgency and authenticity—to many of punk’s early progenitors, punk had died.</p>

            <p>But if punk is dead, the spirit of punk still lives on, in all its transgressive, DIY, no-compromises, often trashy glory. With this series I have attempted to display some of the best products of this ethos. It’s called “Punks Behind the Camera,” not “Punks in Front of the Camera,” for this reason: in this series you will not find conventional movies that feature punks merely as stock characters, or documentaries historicizing the punk phenomenon. The films I have chosen are (perhaps with the exception of WE ARE THE BEST!) films born from punk auteurs and their spiritual progeny. These films, and this movement, have had a profound impact on the development of the indie film in the last half century: SMITHEREENS was the first American indie to win the Palm D’Or, LIQUID SKY, an avant-garde low-budget movie made by Soviets in America was a runaway commercial success, and New Queer Cinema simply would not have come to be without Bruce LaBruce or the trailblazing spirit of punkdom. Yet despite their importance and popularity, most of these films are still hard to find. Many titles are not on streaming platforms, and theatrical retrospectives are even more rare. Take this series as an opportunity for introduction to the urgency and fearless experimentation of a canon that defies expectation.</p>
        </div>
        
