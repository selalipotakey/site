          <h2>Smithereens (1982)</h2>
          <h3>Susan Seidelman &middot; 93m &middot; 35mm</h3>
          <p>In Susan Seidelman’s feature directorial debut, young and talentless Wren (Susan Berman) has dreams of becoming famous in downtown New York’s disappearing punk scene. In the meantime, she gets entangled with drifters and washed-up punk rocker Eric (Richard Hell), who makes promises of leaving to L.A. where punk is still alive. Shot on 16mm with a budget of $40,000, this lo-fi gem was the first American indie to be nominated for the Palme d’Or.</p>
