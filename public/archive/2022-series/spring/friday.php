          <h1>FRIDAY - In the Mood for Wong Kar-wai</h1>
          <h3>Programmed by: Michelle Chow</h3>

            <p>One of the most beloved and influential filmmakers globally, Wong Kar-wai is known for moody, dazzling masterpieces. This retrospective takes the avid theater-goer through a tour of eight of Wong’s ten feature films. From his debut and his most recent feature, it spans a quarter-century of one of the greatest careers in contemporary cinema. </p>

            <p>His body of work ranges widely (from crime thriller to wuxia to romantic drama), but when presented together, his films cohere into a comprehensive, multi-faceted whole; Wong has described his oeuvre as "different episodes of one movie." This consistency is in part thematic–few directors have so perfectly distilled yearning on the big screen, and a sense of wistful, doomed romanticism stretches throughout his works. (As he puts it, “we love what we can't have, and we can't have what we love.”) </p>

            <p>Wong’s long relationships with his collaborators also give his films their sense of cohesion. Viewers will see the same names pop up in the credits of his films–cinematographer Christopher Doyle lends heartbreakingly beautiful visuals and swooning camerawork to six of the films in this series, and several actors (such as Maggie Cheung and Tony Leung) make repeat appearances. </p>

            <p>When Wong was asked for his thoughts on the future of film, he replied, "I'm tired of all this ‘cinema is dead’ shit. People enjoy watching movies, period.” With that, we hope you’re in the mood for Wong Kar-wai!</p>
