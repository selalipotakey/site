          <h2>Tampopo (1985)</h2>
          <h3>Juzo Itami &middot; 114m &middot; DCP</h3>
          <p>A young Ken Watanabe plays a trucker who helps Tampopo, a down-on-her-luck ramen shop proprietress, on her way to noodle mastery in this rollicking culinary ride that eschews plot and genre alike. Perhaps <i>Tampopo</i>’s biggest through-line is instead the endless visual feast that threads together its episodic sketches spanning comedy, gustatory erotica, and playful meditations on human nature—a mix Itami never fails to bring to a satisfying sensory boil.</p>
