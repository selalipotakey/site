          <h1>SUNDAY - Food the Common Tongue: Loves, Rages, and Delights of Gastro-Cinema</h1>
          <h3>Programmed by: Ursula Rigberg Wagner</h3>

            <p>Despite its centrality to human existence, it is relatively rare to find movies that truly center on food. Perhaps this is because culinary experience is sensorial in a way that does not easily translate to film. The challenge for filmmakers, then, is to do more than recreate food aromas, textures, and flavors in our imaginations. The very best culinary movies go deeper and give us the emotional experience of cooking and eating. They also plunge us into the cultures that have given rise to these foods, and the communities they nurture.</p>

            <p>In movies, as in life, food should provide more than sustenance. It communicates feelings characters cannot express in words, and it awakens emotions others did not know they possessed. Some onscreen chefs sublimate their secret desires into their dishes, while others cook to make bold public statements.</p>

            <p>The titles in this series represent a diverse array of international filmmakers, and they span as many moods as they do cuisines. We begin, perversely, with the power of <i>haute cuisine</i> to repulse – to represent greed, punishment, and eventually vengeance (<i>The Cook, the Thief, His Wife, & Her Lover</i>). We then travel to Japan (<i>Tampopo</i>), India (<i>The Lunchbox</i>), France by way of Denmark (<i>Babette’s Feast</i>), Russia by way of Italy (<i>I Am Love</i>), and Italy by way of both Germany (<i>Mostly Martha</i>) and New Jersey (<i>Big Night</i>). Along the way, we stop here in Chicago for some much-needed <i>Soul Food</i>. And for dessert, we have every kind of American pie (<i>Waitress</i>).</p>
        </div>
        
