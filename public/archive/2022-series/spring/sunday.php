          <img src="/images/2022spring/i-am-love-2009.jpeg" alt="I Am Love (2009) still">
          <h2>I Am Love (2009)</h2>
          <h3>Luca Guadagnino &middot; 120m &middot; 35mm</h3>
          <p>Tilda Swinton and Luca Guadagnino wrote this film over the course of 11 years, and their attempt to revive classical melodrama results in a love story that is tangled, yet simple. At its center is Emma (Swinton), the Russian wife of an Italian aristocrat. Inspired by her daughter’s coming out, she opts to follow her own heart as well: to be with her son’s best friend. It's a homey bowl of Russian soup that triggers the unraveling of this family empire.</p>
