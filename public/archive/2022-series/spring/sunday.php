          <h1>7:00PM Sunday, April 24th</h1>
          <img src="/images/2022spring/the-lunchbox-2013.jpg" alt="The Lunchbox (2013) still">
          <h2>The Lunchbox (2013)</h2>
          <h3>Ritesh Batra &middot; 104m &middot; 35mm</h3>
          <p>This quiet drama broke out of Bollywood in 2013, defying expectations as Indian and non-Indian audiences worldwide embraced its thoughtful, grown-up approach to romance. Ila (Nimrat Kaur) attempts to fix her marriage by stuffing love letters and home-cooked meals into lunchboxes for her husband. By mistake, they are delivered to a stranger (Irrfan Khan of <i>Life of Pi</i> and <i>Slumdog Millionaire</i>), and a correspondence nourishing to both body and soul begins.</p>
