          <h2>Babette's Feast (1987)</h2>
          <h3>Gabriel Axel &middot; 102m &middot; Digital</h3>
          <p>Babette, a mysterious Parisian, washes up on the shores of a remote Danish village and offers to keep house for its two elderly matriarchs in exchange for shelter. Her classical French cooking skills threaten to break through their deep repression and send them into a state of ecstasy. Based on a short story by Isak Dinesen, this film epitomizes food’s power to give us religious experience and to strip away the confines of religion entirely.</p>
