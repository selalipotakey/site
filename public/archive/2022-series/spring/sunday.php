          <h2>Soul Food (1997)</h2>
          <h3>George Tillman Jr. &middot; 115m &middot; 35mm</h3>
          <p>Shot in Chicago, this story of a Black family unfolds over weekly Sunday dinners and is told through the eyes of 11-year-old Ahmad. The writer-director was inspired by his own family, especially his Big Mama. Her feasts of fried chicken, catfish, mac and cheese, cornbread, and black-eyed peas are lovingly recreated here and spiced with plenty of family drama. Babyface produced the film, and Vivica A. Fox, Vanessa Williams, and Nia Long play Ahmad’s mom and aunts.</p>
