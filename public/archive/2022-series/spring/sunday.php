          <h2>Big Night (1996)</h2>
          <h3>Campbell Scott and Stanley Tucci &middot; 107m &middot; 35mm</h3>
          <p>Anthony Bourdain once called this wistful comedy “the best restaurant movie ever.” Tony Shalhoub and Stanley Tucci play Primo and Secondo, emigrants from Calabria to the Jersey Shore. To save their authentic but struggling restaurant, they must cook the feast of their lives, whose pièce de résistance is the grand <i>timpano</i>. Tucci co-wrote the script and co-directed a cast that includes Minnie Driver, Ian Holm, Allison Janney, and Isabella Rossellini.</p>
