          <h2>The Living End (1992)</h2>
          <h3>Gregg Araki &middot; 84m &middot; DCP</h3>
          <p>Two HIV-positive, gay lovers drive across the country and commit crimes that target homophobes in <i>The Living End</i>. Luke (Mike Dytri) is a hustler on the run from the law, and Jon (Craig Gilmore) is a mild mannered film critic. Written, directed, shot, and edited by New Queer Cinema enfant terrible Gregg Araki, <i>The Living End</i> offers a very funny, very angry, and very '90s spin on the Bonnie and Clyde myth. The film was later nominated at Sundance.</p>
