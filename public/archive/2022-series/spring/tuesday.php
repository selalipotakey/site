          <h2>Basic Instinct (1992)</h2>
          <h3>Paul Verhoeven &middot; 128m &middot; DCP</h3>
          <p>Provocateur Paul Verhoeven’s blockbuster neo-noir tells the story of a police detective (Michael Douglas) investigating a crime novelist (Sharon Stone) accused of murdering her boyfriend with an icepick. As the investigation intensifies, so does the steam between the two smooth operators. Joe Ezterhaus’s notoriously sleazy <i>Vertigo</i>-inspired script provides the perfect vehicle for Stone to give the mother of all femme fatale performances.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Tuesday, April 5th</h1>
          <img src="/images/2022spring/one-false-move-1992.jpeg" alt="One False Move (1992) still">
          <h2>One False Move (1992)</h2>
          <h3>Carl Franklin &middot; 106m &middot; 35mm</h3>
          <p>Ruthless outlaws Ray (Billy Bob Thornton), Fantasia (Cynda Williams), and Pluto (Michael Beach) are on a crime spree stretching from Los Angeles to Fantasia’s hometown of Star City, Arkansas. Meanwhile, the overeager Star City sheriff Dale “Hurricane” Dixon (Bill Paxton) and a team of federal marshals prepare for a showdown. Director Carl Franklin delivers a nuanced portrait of both sides of the law from a script that was co-written by Thornton.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Tuesday, April 12th</h1>
          <img src="/images/2022spring/raising-cain-1992.jpeg" alt="Raising Cain (1992) still">
          <h2>Raising Cain (1992)</h2>
          <h3>Brian De Palma &middot; 91m &middot; 35mm</h3>
          <p>Dr. Nix (John Lithgow) is a child psychologist with multiple personality disorder, who begins kidnapping children for research purposes in this nightmarish neo-noir that gleefully combines elements of Hitchcock’s <i>Psycho</i> (1960) and Powell’s <i>Peeping Tom</i> (1960). Made in the wake of the critical and financial failure of <i>Bonfire of the Vanities</i> (1990), <i>Raising Cain</i> is a return to the bold pastiche that defined De Palma's output during the '70s and '80s.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Tuesday, April 19th</h1>
          <img src="/images/2022spring/swoon-1992.jpeg" alt="Swoon (1992) still">
          <h2>Swoon (1992)</h2>
          <h3>Tom Kalin &middot; 94m &middot; 35mm</h3>
          <p>Tom Kalin’s retelling of the infamous case of Leopold and Loeb, convicted murderers (and University of Chicago students) who inspired Hollywood classics such as <i>Rope</i> (1948) and <i>Compulsion</i> (1959). A classic of New Queer Cinema, <i>Swoon</i> examines the often ignored romantic relationship between the two young men. Ellen Kuras, cinematographer of <i>Eternal Sunshine of the Spotless Mind</i> (2003) provides stunning black and white cinematography.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Tuesday, April 26th</h1>
          <img src="/images/2022spring/star-time-1992.jpg" alt="Star Time (1992) still">
          <h2>Star Time (1992)</h2>
          <h3>Alexander Cassini &middot; 85m &middot; DCP</h3>
          <p>Despite premiering at Sundance in 1992 and receiving an enthusiastic review from Chicago critic Jonathan Rosenbaum during its run at the Music Box, Alexander Cassini’s <i>Star Time</i> has remained a cult film for over three decades. Recently restored by the American Genre Film Archive, the film offers a wickedly smart and frequently weird critique of news media and the serial killer genre without romanticizing mental illness or indulging in gruesome imagery.</p>
        </div>
        
