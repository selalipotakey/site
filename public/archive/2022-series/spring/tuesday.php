          <h1>TUESDAY - Neo-Noir ‘92</h1>
          <h3>Programmed by: Zachary Vanes</h3>

            <p>Neo-noir was everywhere in 1992. In Hollywood, Paul Verhoeven’s BASIC INSTINCT was the 4th highest  grossing film of the year. Independent giant New Line Cinema and its new indie division Fine Line Features released an ambitious slate of noirish thrillers culled from the festival circuit including films by newcomers, such as Tom Kalin’s SWOON, and the work of seasoned veterans, like Robert Altman’s THE PLAYER and Paul Schrader’s LIGHT SLEEPER.  For New Line’s releases and other independently produced films like ONE FALSE MOVE, THE LIVING  END, and STAR TIME, neo-noir provided the vehicle for stories of queer relationships, criticisms of the 24/7 media landscape, and reflections on the intersection of race and class in American life.  </p>

            <p>NEO-NOIR ‘92 celebrates the 30th anniversary of nine films that defined film noir for a new generation. Blending the macabre thrills of Alfred Hitchcock with the formal rule breaking of the French New Wave, these psychological crime films filled art houses and shopping mall megaplexes alike in the early 90s. Populated with a rogues’ gallery of serial killers, doomed lovers, femme fatales, and sleazy cops, the films are both a rejection of the bland respectability of the Christian Right and an expression of the country’s post-Cold War identity crisis. Gone is the guilty conscience of the classic noir - in its place is a devil-may-care embrace of the American underbelly. </p>
        </div>
        
