          <h2>Batman Returns (1992)</h2>
          <h3>Tim Burton &middot; 126m &middot; 35mm</h3>
          <p>The sequel to the 1989 <i>Batman</i> pits the caped crusader against criminal masterminds Penguin (Danny DeVito) and Catwoman (Michelle Pffeifer) during Christmastime in Gotham City. Burton blends the gothic twee of <i>Beetlejuice</i> and <i>Edward Scissorhands</i> with the postmodern noir of the Batman universe, and with supporting performances from Christopher Walken and an army of rocket-wielding penguins, this film is a blockbuster that isn’t afraid to get weird.</p>
