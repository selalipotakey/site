          <h3>Programmed by: Eric Guzman</h3>

            <p>Projecting Paranoia is a reflection on manipulation and control. The goal is not to seek the truth but to see shades of truth. These projections show things that "they" don't want you to see. The filmmakers that made these films understood that to be paranoid is to believe in contradictions. Within these contradictions lies a hidden history that must be experienced to be understood. A feeling of dread that lies beneath the surface. Something has gone wrong and we all feel it, both in the past and in the future. Was it the creation of the nuclear bomb? Or the various political assassinations of the 1960s? Political scandals and fake wars. The fog is setting in (or the smoke?) and the lessons are no longer clear. What did we learn from all this?</p>
