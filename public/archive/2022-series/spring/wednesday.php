          <h2>Heaven Can Wait (1978)</h2>
          <h3>Warren Beatty and Buck Henry &middot; 101m &middot; DCP</h3>
          <p>A critical and commercial success upon release, this film brought May her first Oscar nomination. Her script, adapted with star and director Warren Beatty, tells the story of Joe Pendleton, an NFL quarterback who is prematurely summoned to heaven by an overeager angel. Joe's efforts to return in a new body are complicated by an elderly industrialist, a beautiful environmental activist, and his own team's run to the Super Bowl in this romantic fantasy.</p>
