          <h1>WEDNESDAY - They Thought I Was a Nice Girl: The Films of Elaine May</h1>
          <h3>Programmed by: Liam Flanigan</h3>

            <p>First rising to prominence as half of iconic comedy duo Nichols and May, Elaine May has worked steadily as a performer, writer, and director over the past sixty years. This series seeks to celebrate May’s work, including the two screenplays for which she earned Oscar nominations as well as early film performances from the 1960s. In addition, special attention is paid to the four feature films she has directed in her career. Although all are wildly different in concept and creation, each is the work of an assured and precise filmmaker whose career has been unfortunately complicated by sexism and studio interference.</p>

            <p>May’s films are the work of a meticulous observer of human shortcomings. Each is full of selfish, greedy characters—particularly men—who betray the trust of others with almost no hesitation. These characters are sometimes deployed in uproarious comedies, including 1971’s <i>A New Leaf</i> or 1972’s <i>The Heartbreak Kid</i>, but May is just as masterful in the realm of drama, as demonstrated in the merciless gangster picture <i>Mikey and Nicky</i> (1976). Of particular note is <i>Ishtar</i> (1987), a dead-on-arrival comedy that is ripe for rediscovery as an ahead-of-its-time masterwork. On the strength of these four movies—each full of vibrant performances complemented by exacting filmmaking—May and her oeuvre more than deserve a place in the canon of New Hollywood.</p>
        </div>
        
