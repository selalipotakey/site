        <div class="screening">
          <h1>7:00PM Wednesday, April 20th</h1>
          <img src="/images/2022spring/wolf-1994.jpeg" alt="Wolf (1994) still">
          <h2>Wolf (1994)</h2>
          <h3>Mike Nichols &middot; 125m &middot; 35mm</h3>
          <p>Although her contributions to the screenplay are uncredited, <i>Wolf</i> marked the rekindling of May's working relationship with director Mike Nichols. The resultant film, a strange blend of romance, horror, and workplace drama, features Jack Nicholson as Will Randall, a New York publishing executive turned werewolf. Michelle Pfeiffer co-stars, Ennio Morricone scores, and the end product offers invigoration and bewilderment in equal measure.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Wednesday, April 27th</h1>
          <img src="/images/2022spring/primary-colors-1998.jpg" alt="Primary Colors (1998) still">
          <h2>Primary Colors (1998)</h2>
          <h3>Mike Nichols &middot; 143m &middot; 35mm</h3>
          <p>Working from May's Oscar-nominated screenplay, Mike Nichols directs this political dramedy about a Southern governor's presidential campaign and multiple sexual scandals. John Travolta and Emma Thompson are the thinly-veiled Bill and Hillary with an all-star supporting cast including Kathy Bates, Allison Janney, and Billy Bob Thornton. Based on reporter Joe Klein's roman à clef, <i>Primary Colors</i> is a swirl of cynicism and hope—just like real politics.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Wednesday, May 4th</h1>
          <img src="/images/2022spring/a-new-leaf-1971.jpg" alt="A New Leaf (1971) still">
          <h2>A New Leaf (1971)</h2>
          <h3>Elaine May &middot; 102m &middot; 35mm</h3>
          <p>In May's directorial debut, Walter Matthau stars as a playboy with a depleted trust fund in desperate need of a rich woman to marry and murder. May also adapted the script and co-stars as Henrietta Lowell, the bumbling botanist whom Matthau targets. The result of several edits and eventual court proceedings between May and Columbia Pictures, <i>A New Leaf</i> features hilarious bitterness, genuine tenderness, and the funniest wedding ever set to celluloid.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Wednesday, May 11th</h1>
          <img src="/images/2022spring/the-heartbreak-kid-1972.jpg" alt="The Heartbreak Kid (1972) still">
          <h2>The Heartbreak Kid (1972)</h2>
          <h3>Elaine May &middot; 119m &middot; 35mm</h3>
          <p>The authority of May's cinematic voice is perhaps never clearer than in <i>The Heartbreak Kid</i>. Working from a Neil Simon screenplay, May crafts a biting comedy about Lenny (Charles Grodin), a newlywed who falls in love with a different woman on his honeymoon. Jeannie Berlin (May's daughter) was nominated for an Oscar for her portrayal of the scorned bride in this restless comedy about disillusionment, sunburns, and the honesty of Midwestern vegetables.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Wednesday, May 18th</h1>
          <img src="/images/2022spring/mikey-and-nicky-1976.jpeg" alt="Mikey and Nicky (1976) still">
          <h2>Mikey and Nicky (1976)</h2>
          <h3>Elaine May &middot; 119m &middot; DCP</h3>
          <p>A chokingly claustrophobic gangster picture, <i>Mikey and Nicky</i> is May's first wholly original film. When a hit is put out on low-level gangster Nicky (John Cassavetes), he calls on his old friend and fellow criminal Mikey (Peter Falk). May creates a stirring portrait of love and enmity set over the course of one endless night. Falk and Cassavetes offer powerhouse performances and the final sequence stays with the viewer long after leaving the theater.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Wednesday, May 25th</h1>
          <img src="/images/2022spring/ishtar-1987.jpg" alt="Ishtar (1987) still">
          <h2>Ishtar (1987)</h2>
          <h3>Elaine May &middot; 107m &middot; 35mm</h3>
          <p>35 years after its legendary initial failure, May's final feature to date has re-emerged as another comedic masterwork and a biting political satire. Dustin Hoffman and Warren Beatty star as two monumentally untalented songwriters trying to be the next Simon and Garfunkel but end up pulled into civil unrest in the fictitious nation of Ishtar. A musical, a buddy comedy, a political thriller, and more, <i>Ishtar</i> has to be seen to be believed.</p>
