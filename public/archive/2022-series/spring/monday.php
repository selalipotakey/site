          <h1>7:00PM Monday, May 16th</h1>
          <img src="/images/2022spring/twice-as-nice-1989.jpg" alt="An Open Window (2005) // Killing Time (1979) // Twice as Nice (1989) still">
          <h2>An Open Window (2005) // Killing Time (1979) // Twice as Nice (1989)</h2>
          <h3>Khady Sylla // Fronza Woods // Jessie Maple &middot; 52m // 15m // 70m &middot; Digital // 16mm // DCP</h3>
          <p><i>An Open Window</i> is a film about mental illness among Dakar’s street wanderers. After shooting, Sylla fell ill, “seeing what others don’t see—the dislocated eye, the ancientness of the glass bubble, a sky descended too low, a horizon far too near. I was living the experience from the inside.” Fronza Wood’s <i>Killing Time</i> is a dark comedy about finding the right outfit to commit suicide in. The New Yorker calls it “among the best short films ever made." After becoming the first Black woman in the New York camera operators' union, Jessie Maple turned to directing. In <i>Twice As Nice</i>, twin basketball players compete to become the first woman in the “MBA.” The film stars real-life powerhouses Pamela and Paula McGhee, who led USC to back-to-back NCAA championships in the 1980s, and Cynthia Cooper-Dyke. Preceded by Onwurah’s Coffee Coloured Children, an affecting meditation on internalized racism.</p>
