          <img src="/images/2022spring/finding-christa-1991.jpg" alt="Remembering Wei Yi-Fang, Remembering Myself (1995) // Finding Christa (1991) still">
          <h2>Remembering Wei Yi-Fang, Remembering Myself (1995) // Finding Christa (1991)</h2>
          <h3>Yvonne Welbon // Camille Billops and James Hatch &middot; 29m // 55m &middot; 16mm // Digital</h3>
          <p>In <i>Remembering Wei Yi-fang</i>, Chicago-born filmmaker Yvonne Welbon’s inventive exploration of ancestral lineage, Welbon contrasts her grandmother’s experience of racism migrating from Honduras to South Dakota with her own years living in Taiwan. Camille Billops’ <i>Finding Christa</i> explores family history and the process of giving her child up for adoption through family photos, home videos, brutally honest interviews with family, and surreal interludes.</p>
