          <h1>MONDAY - An Open Window: Black Female Directors Across the Diaspora</h1>
          <h3>Programmed by: Erisa Apantaku and J. Michael Eugenio</h3>

            <p>“There’s something really important about stories about Black women and girls being told by Black women. [They] give you a reflection, as opposed to an interpretation, of what our lives are like.”</p>

            <p>-Ava DuVernay</p>

            <p>Work by contemporary Black women directors such as Dee Rees, Nia DaCosta, and Ava DuVernay is on the rise in the past decade. These directors have received recognition and wide releases, leading to a resurrection and rebirth of work from predecessors like Julie Dash and Kasi Lemmons. But there is always more to uncover. Black women have been involved in filmmaking since the origin of the medium, and their films exist across the Black Diaspora. From documenting political resistance to personal struggle, the short and feature-length films in this series demonstrate the myriad aesthetics employed by Black women directors to tell their stories over the past five decades. In “An Open Window,” we invite you to take a glimpse into work that has often been left unseen.</p>
            
            <p><b>Sponsored by South Side Projection and Arts + Public Life</b></p>
