          <img src="/images/2022spring/the-two-faces-of-a-bamileke-woman-2018.jpg" alt="The Two Faces of a Bamileke Woman (2018) still">
          <h2>The Two Faces of a Bamileke Woman (2018)</h2>
          <h3>Rosine Mbakam &middot; 76m &middot; DCP</h3>
          <p>In <i>Two Faces of a Bamiléké Woman</i>, writer-director Rosine Mbakam composes an intimate documentary account of life from a woman's perspective in Cameroon. Told through interactions with her mother, Mbakam traces the different cultures in the village she was born in and the capital city, Yaoundé. The women recount horrific tales from the French colonial war and from their arranged marriages, but Mbakam finds solidarity in their shared struggle.</p>
