          <h2>Towards Tenderness (2016) // Bodily Function (1995)</h2>
          <h3>Alice Diop // Jocelyn Taylor &middot; 113m // 14m &middot; DCP // Digital</h3>
          <p>Alice Diop’s <i>Towards Tenderness</i> features audio interviews with four young men about sex and masculinity overlaid with images of their daily lives. Three narratives exploring sexual expression and body image interweave in queer filmmaker Jocelyn Taylor’s <i>Bodily Function</i>. <i>Puk Nini</i> by Fanta Régina Nacro—the first Burkinabè woman to direct a feature film—highlights women’s independence and solidarity, showing adultery from men and women’s viewpoints.</p>
