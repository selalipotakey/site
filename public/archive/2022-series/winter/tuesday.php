          <h1>7:00PM Tuesday, February 1st</h1>
          <img src="/images/2022winter/cabin-in-the-sky-1943.jpg" alt="Cabin in the Sky (1943) still">
          <h2>Cabin in the Sky (1943)</h2>
          <h3>Vincente Minelli and Busby Berkeley &middot; 98m &middot; 35mm</h3>
          <p>When compulsive gambler Little Joe (Eddie Anderson, AKA <i>Jack Benny</i>’s "Rochester") dies in a fight, heaven and hell compete for his soul. Will he stay with his loyal wife (singing star Ethel Waters) or go with a sultry songstress (Lena Horne)? Hit songs like “Taking a Chance on Love” and “Happiness Is a Thing Called Joe” alongside performances by the Hall Johnson Choir and the Duke Ellington band add to the delight of this all-black musical.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Tuesday, February 8th</h1>
          <img src="/images/2022winter/young-man-with-a-horn-1950.jpg" alt="Young Man With A Horn (1950) still">
          <h2>Young Man With A Horn (1950)</h2>
          <h3>Michael Curtiz &middot; 112m &middot; 35mm</h3>
          <p>Jazz-obsessed Rick (Kirk Douglas) pretends to play his trumpet while an unseen Harry James plays the actual notes in a tale based on the legend of 1920s star cornetist Bix Beiderbecke. Rick has to perform in corny dance bands while he yearns to play the jazz he loves. Moreover, he’s torn between the seductions of Ms. Right (Doris Day), Ms. Wrong (Lauren Bacall), and Demon Rum—you’ll never guess which wins. Watch for a glimpse of young Dexter Gordon.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Tuesday, February 15th</h1>
          <img src="/images/2022winter/shadows-1935.jpg" alt="Symphony in Black: A Rhapsody of Negro Life (1935) // Shadows (1959) still">
          <h2>Symphony in Black: A Rhapsody of Negro Life (1935) // Shadows (1959)</h2>
          <h3>Fred Waller // John Cassavetes &middot; 9m // 87m &middot; DCP</h3>
          <p>A rare film of a suite composed by Duke Ellington, with his band and Billie Holiday in her film debut. Jazz, youthful ambition, interracial love, and making the scene are among the themes of Cassavetes’ first feature film as a director. Two African-American brothers, an immature trumpeter and an unstylish singer, and their sister, a writer, struggle amidst New York’s Beat Generation, to the music of solo saxophonist Shafi Hadi and Charles Mingus’s band.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Tuesday, February 22nd</h1>
          <img src="/images/2022winter/the-connection-1962.jpeg" alt="The Connection (1962) still">
          <h2>The Connection (1962)</h2>
          <h3>Shirley Clarke &middot; 110m &middot; DCP</h3>
          <p>“Men Held Captive by the Power of Drugs” blares the ad for Clarke’s innovative first feature. A painfully realistic adaptation of an off-Broadway play, <i>The Connection</i> shows desperate junkies awaiting their next fix. At times characters speak directly to the camera despite a naive film director's cues to “act natural.” This raw film stars Warren Finnerty and co-stars composer-pianist Freddie Redd’s quartet, including the alto sax legend Jackie McLean.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Tuesday, March 1st</h1>
          <img src="/images/2022winter/round-midnight-1986.jpg" alt="Round Midnight (1986) still">
          <h2>Round Midnight (1986)</h2>
          <h3>Bertrand Tavernier &middot; 133m &middot; Digital</h3>
          <p>Beloved tenor sax Long Tall Dexter Gordon, a real-life African-American expatriate in Europe, stars as a declining African-American expatriate tenor sax in Paris. Tragedy and black humor mark his slow slide into permanent oblivion, interrupted by the ministrations of an adoring fan. It’s a fictional version of pianist Bud Powell’s story, and jazz favorites like Herbie Hancock, Freddie Hubbard, Billy Higgins, and Bobby Hutcherson comprise most of the cast.</p>
