          <h2>Mo' Better Blues (1990)</h2>
          <h3>Spike Lee &middot; 129m &middot; 35mm</h3>
          <p>Trumpeter Bleek Gilliam, portrayed by Denzel Washington, leads a dream of a life, fronting his own hard-bop band in a long-running nightclub gig and successfully romancing two lovers who don’t know each other. How can his sidemen (among them Wesley Snipes), his weaselly manager (Spike Lee), and the two ladies possibly bring him sorrow? Trumpeter Terence Blanchard, a Lee-film favorite, and the Branford Marsalis Quintet make the music that makes the story.</p>
