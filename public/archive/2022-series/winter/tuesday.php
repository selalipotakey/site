          <h1>TUESDAY - Nights of the Swingers! Jazz in Film</h1>
          <h3>Programmed by: John Litweiler</h3>

            <p>"Does Jazz Put the Sin in Syncopation?" – title of a 1921 <i>Ladies Home Journal</i> article</p>

            <p> </p>

            <p>As rap and rock music scandalized your parents and grandparents, so jazz was young people's music in your great-grandparents' time.  Born early in the 20th century, this essentially African-American art form grew up beside the movies when both were called low-life entertainment by guardians of public morals.  Nevertheless, during the Depression and World War 2 years, jitterbugging youth made swing music wildly popular. The sordid atmospheres of crime dramas often called for jazz soundtracks and scenes in dim jazz joints; but it took all-black musicals such as <i>Cabin in the Sky</i> and <i>Stormy Weather</i> to convey the joy of jazz. In more modern times, as jazz and film became widely accepted as great art forms, jazz came into films' daylight. So this series offers the hardness of the jazz life as well as the redeeming qualities of fine art.</p>
        </div>
        
