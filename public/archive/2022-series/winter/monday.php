          <h1>MONDAY - Which Side Are You On? Labor and Collective Action On Film</h1>
          <h3>Programmed by: John Kaderbek and Katerina Korola</h3>

            <p><i>Which Side Are You On?: Visions of Labor and Collective Action on Film</i> is a collaboration between members of three different labor unions on campus. “Labor Film” is a very broad category, and this series attempts to capture that breadth by incorporating films from a range of genres, geographic, and historical contexts. Some of the films, such as the acclaimed Soviet silent drama, <i>Battleship Potemkin</i>, are well-known classics, while others are very rarely screened. Together, the films offer a diverse and wide-ranging picture of the different forms of work, collective struggle, and solidarity across the history of cinema, while at the same time speaking to the intersection of labor rights, racial justice, and women’s liberation.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Monday, January 24th</h1>
          <img src="/images/2022winter/salt-of-the-earth-1954.png" alt="Salt of the Earth (1954) still">
          <h2>Salt of the Earth (1954)</h2>
          <h3>Herbert Biberman &middot; 94m &middot; 16mm</h3>
          <p>Made at the height of the red scare by a crew of blacklisted filmmakers, this film is one of the most audacious projects of political cinema in film history as a dramatization of a strike of Mexican-American zinc miners seeking a living wage and safe working conditions. Narrated from the perspective of a miner’s wife, the film sounds a powerful call for labour rights and racial equality and raises questions of gender and women’s liberation.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Monday, January 31st</h1>
          <img src="/images/2022winter/battleship-potemkin-1925.jpg" alt="Battleship Potemkin (1925) // Redes (1936) still">
          <h2>Battleship Potemkin (1925) // Redes (1936)</h2>
          <h3>Sergei Eisenstein // Emilio Gómez Muriel and Fred Zinnemann &middot; 66m // 65m &middot; Digital</h3>
          <p>This double feature brings together two films that depict the coming together of a collective to confront exploitation. A monument of early Soviet cinema, Sergei Eisenstein’s <i>Battleship Potemkin</i> presents a dramatization of the naval mutiny that sparked the First Russian Revolution in 1905. Characterized by striking graphic compositions and inventive montage, the film would inspire leftist filmmakers across the globe, including the American photographer Paul Strand, who acted as the cinematographer for Fred Zinnemann and Emilio Gómez Muriel’s <i>Redes</i>, which explores the political awakening of a group of fishermen. Filled with stunning images of waves, clouds, and laboring bodies, this recently restored film offers a lyrical but hard-hitting portrayal of work and resistance on the Gulf of Mexico.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Monday, February 7th</h1>
          <img src="/images/2022winter/the-gleaners-and-i-2000.jpeg" alt="The Gleaners and I (2000) still">
          <h2>The Gleaners and I (2000)</h2>
          <h3>Agnès Varda &middot; 82m &middot; DCP</h3>
          <p>In this documentary, Agnès Varda takes the practice of gleaning—the act of gathering leftover food and discarded objects—as the occasion for a playful and self-reflexive inquiry into practices of scavenging, reuse, and art-making at the margins of modern-day consumerism. The film presents a fascinating portrayal of labor and collectivity outside the market while at the same time offering a vision of abundance and beauty in the least expected places.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Monday, February 21st</h1>
          <img src="/images/2022winter/two-days-one-night-2014.jpeg" alt="Two Days, One Night (2014) still">
          <h2>Two Days, One Night (2014)</h2>
          <h3>Luc Dardenne and Jean-Pierre Dardenne &middot; 95m &middot; DCP</h3>
          <p>After a medical leave of absence, Sandra (Marion Cotillard) learns that her coworkers have been asked to choose between her returning to work and a €1,000 bonus each. With only two days before the vote, she must visit each of them to plead her case. A ​moving dramatization of the possibility of solidarity—and the forces that aim to thwart it—the film is the most optimistic entry in the Dardenne brothers’ chronicle of the lives of the Belgian working class.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Monday, February 28th</h1>
          <img src="/images/2022winter/harlan-county-usa-1976.png" alt="Harlan County, USA (1976) still">
          <h2>Harlan County, USA (1976)</h2>
          <h3>Barbara Kopple &middot; 103m &middot; Digital</h3>
          <p>Barbara Kopple's Academy Award winning documentary of the Brookside Strike depicts mineworkers struggling for solidarity amongst injunctions, scabs, and armed thugs perpetrated by the Duke Power Company. Shot in 1973, the film still has contemporary relevance as miners in Alabama have been on strike for several months against Warrior Met Coal, and both the company and the union are deploying many of the same tactics depicted in this film.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Monday, March 7th</h1>
          <img src="/images/2022winter/finally-got-the-news-1970.jpg" alt="Finally Got the News (1970) still">
          <h2>Finally Got the News (1970)</h2>
          <h3>Rene Lichtman, Peter Gessner, and Stewart Bird &middot; 55m &middot; Digital</h3>
          <p>Filmed from 1969-70, this documentary offers a look at the League of Revolutionary Black Workers as they organized in Detroit against both the big car companies and their racist and anti-democratic union leadership. The League and their view that both the oppression and the potential power of Black Americans is located in the workplace stands in contrast with many New Left groups of their era, including the white activist documentarians.</p>
