          <h1>FRIDAY - Classics of South Korean Cinema</h1>
          <h3>Programmed by: William Carroll</h3>

            <p>When Bong Joon-ho’s <i>Parasite</i> began gaining Oscar buzz in late 2019, I got inspired to do a series of Classic South Korean Cinema. Thanks to the hard work of preservation and restoration by the Korean Film Archive, many of these films are available for screening, and they deserve to be viewed in the best way possible.</p>

            <p>South Korea developed a vibrant industry by the mid-1950s, even as the country was struggling to recover from colonialism and the Korean Civil War. Talented filmmakers made  noirish thrillers, historical films, Manchurian Action films, horror films, war films and neorealist dramas. The most popular was melodrama, though there was a wide stylistic variety—just think of the elegant restraint of <i>The Coachman</i> compared with the childhood innocence depicted in <i>My Mother and Her Guest</i>. Our series also includes the two best known and most acclaimed films of the era—the domestic thriller <i>The Housemaid</i> and the neorealist drama <i>Aimless Bullet</i>, both key influences on South Korean filmmakers to this day.</p>
            
            <p><b>This series is co-sponsored by the Center for East Asian Studies at the University of Chicago with generous support from a Title VI National Resource Center Grant from the U.S. Department of Education.</b></p>
