          <h1>7:00PM Friday, January 28th</h1>
          <img src="/images/2022winter/the-housemaid-1960.jpg" alt="The Housemaid (1960) still">
          <h2>The Housemaid (1960)</h2>
          <h3>Kim Ki-young &middot; 111m &middot; Digital</h3>
          <p>In Kim Ki-young's intensely psychological domestic thriller, a composer hires a housemaid to assist his newly pregnant wife. The family begins to spin out of control as the two women vie for the composer's attention amidst his failures as a husband and a lover. <i>The Housemaid</i> is perhaps the best-known film of South Korean cinema’s first Golden Age, and Bong Joon-ho has cited the film as a key influence on <i>Parasite</i> and one of his all-time favorites.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Friday, February 4th</h1>
          <img src="/images/2022winter/my-mother-and-her-houseguest-1961.png" alt="My Mother and Her Houseguest (1961) still">
          <h2>My Mother and Her Houseguest (1961)</h2>
          <h3>Shin Sang-ok &middot; 102m &middot; Digital</h3>
          <p>One of the great melodramas of the first Golden Age of South Korean cinema. A widowed mother, living with her young daughter, her mother-in-law, and her housekeeper, takes in a boarder and develops a romance with him. The film is told from the perspective of six-year-old Ok-hee, who witnesses the interactions of her mother with the boarder and acts as their messenger, without fully understanding their feelings or the social ramifications of their love.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Friday, February 11th</h1>
          <img src="/images/2022winter/madame-freedom-1956.png" alt="Madame Freedom (1956) still">
          <h2>Madame Freedom (1956)</h2>
          <h3>Han Hyeong-mo &middot; 125m &middot; Digital</h3>
          <p>Professor Jang and his wife Sunyoung outwardly appear to be a respectable upper-class Seoulite couple, but their marriage is tested as each entertains the possibility of an affair. As Sunyoung gives in to temptation, her indulgences lead to her downfall. The film's scandalousness made it both incredibly popular and controversial when it was released, cementing its position as a classic melodrama from the first Golden Age of South Korean Cinema.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Friday, February 25th</h1>
          <img src="/images/2022winter/the-coachman-1961.jpg" alt="The Coachman (1961) still">
          <h2>The Coachman (1961)</h2>
          <h3>Kang Dae-jin &middot; 120m &middot; DCP</h3>
          <p>The first Korean film to win an award at an international film festival. <i>The Coachman</i> depicts the life of a single father who makes his living by operating a horse-drawn cart in Seoul as his way of life becomes obsolete. Like <i>Aimless Bullet</i>, this film presents a frank view of those left behind in modernizing South Korea, albeit with a greater sense of hope for the future. Ranked #5 on the Korean Film Archive’s 2014 list of Best Korean films.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Friday, March 4th</h1>
          <img src="/images/2022winter/the-daughters-of-kims-pharmacy-1963.png" alt="The Daughters of Kim's Pharmacy (1963) still">
          <h2>The Daughters of Kim's Pharmacy (1963)</h2>
          <h3>Yu Hyun-mok &middot; 109m &middot; Digital</h3>
          <p>In the beautiful city of Tongyeong along Korea’s southeastern coast, pharmacist Kim is convinced that an old curse on his family is the source of him and his four daughters' misfortune. Ostensibly a family drama, <i>The Daughters of Kim’s Pharmacy</i> (dir. Yu Hyun-mok, also <i>Aimless Bullet</i>) features eerily atmospheric sets, shamanism, and axe-wielding opium addicts in a narrative that rises to an unpredictable frenzy as it progresses to its finale.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Friday, March 11th</h1>
          <img src="/images/2022winter/a-flower-in-hell-1958.jpeg" alt="A Flower in Hell (1958) still">
          <h2>A Flower in Hell (1958)</h2>
          <h3>Shin Sang-ok &middot; 86m &middot; DCP</h3>
          <p>A picture of 1950s South Korea, <i>A Flower in Hell</i> depicts a society ravaged in the aftermath of the Korean War. Freshly out of the military, young Dong-shik comes to Seoul to convince his brother Young-shik, who steals American army base supplies with the help of local prostitutes, to come home and leave his thievery behind. Instead, Dong-shik finds himself seduced by his brother's girlfriend, the prostitute Sonia, who helps Young-shik in his operations.</p>
