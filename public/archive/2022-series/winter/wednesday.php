          <img src="/images/2022winter/cowards-bend-the-knee-2003.jpg" alt="Sissy Boy Slap Party (1995) // Cowards Bend the Knee (2003) still">
          <h2>Sissy Boy Slap Party (1995) // Cowards Bend the Knee (2003)</h2>
          <h3>Guy Maddin // Guy Maddin &middot; 6m // 64m &middot; Digital</h3>
          <p>Originally intended to be 10 short films seen through a series of peepholes, this is the first in Maddin's autobiographical trilogy which includes <i>Brand Upon the Brain!</i> and <i>My Winnipeg</i>. Shot on Super 8 in the style of a frenetic silent film, we visit themes of betrayal, revenge and regret. Maddin blends melodrama and pathos with humor—featuring a ghost, a beauty salon-bordello combo, and his signature hockey players. Preceded by <i>Sissy Boy Slap Party</i>.</p>
