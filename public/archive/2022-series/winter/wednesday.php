          <img src="/images/2022winter/dracula-pages-from-a-virgins-diary-2002.jpg" alt="Dracula, Pages from a Virgin's Diary (2002) still">
          <h2>Dracula, Pages from a Virgin's Diary (2002)</h2>
          <h3>Guy Maddin &middot; 75m &middot; 35mm</h3>
          <p>Maddin underlines the erotic horror lurking in Bram Stoker's story as well as infusing it with his signature humor with title cards like, "She's filled with polluted blood!" Dracula is interpreted by the Royal Winnipeg Ballet, set to Mahler and shot like a silent film on low-res formats including 16mm and Super 8. Admittedly not a fan of ballet or Dracula when first commissioned, Maddin transcends preconceptions about either into a work all his own.</p>
