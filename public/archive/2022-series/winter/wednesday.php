          <h1>WEDNESDAY - A Guy Maddin Retrospective…</h1>
          <h3>Programmed by: Penny Folger</h3>

            <p>Guy Maddin's films have been referred to by critic Jonathan Rosenbaum as "deranged heterosexual camp," but the label seems to have been given with a great deal of affection for the director's work. Stepping into a Guy Maddin film is often like stepping into another universe entirely: one laced with melodrama, pathos, humor, hockey players, and gentlemen who use fish guts as hair pomade. Often working with very low budgets, Maddin uses experimental techniques such as smearing Vaseline around the edges of his lenses; shooting in a wild collage of varying low-res formats; frenetic editing and theatrical, often humorous title cards, in a whirlwind style that not only acts as a throwback to our own cinema history, but also transforms it into a more contemporary, dreamlike, and humorous vision that is unlike any other.</p>

            <p> </p>

            <p>A kind of melodrama that is often lathered to the point of deadpan comedy, his movies involve topics including but not limited to: amnesia, sexual repression, romantic despair, revenge, sinister orphanages, incestuous longings, amputees, hailstorms of bunny rabbits, the aforementioned ice hockey players, ballet, and a baroness with glass beer bottle legs who says about her search to find the saddest song on Earth, “If you’re sad and like beer, I’m your lady.” </p>

            <p>Come see his movies on the big screen; they’re well deserving of it.</p>
