          <h2>My Winnipeg (2007)</h2>
          <h3>Guy Maddin &middot; 80m &middot; 35mm</h3>
          <p>Originally titled "Love Me, Love My Winnipeg," this surrealist pseudo-documentary/mockumentary mythologizes Winnipeg history and Maddin's own childhood through the blending of fact, fantasy and memory. Claiming Winnipeg has 10x more sleepwalkers than any other city, he reenacts childhood memories at his parents' 65th anniversary celebration (despite his father having been dead for some years) in their old family home above a beauty parlor.</p>
