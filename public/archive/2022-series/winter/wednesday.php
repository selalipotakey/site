          <h2>Archangel (1991)</h2>
          <h3>Guy Maddin &middot; 90m &middot; 35mm</h3>
          <p>In the Arkhangelsk region of Russia in 1919, a one-legged Canadian solider and victim of amnesia (by way of mustard gas) mistakes a nurse for his dead wife. Meanwhile, her own husband, suffering the same affliction, forgets that he is married. Filmed in a style reminiscent of the late silent to early talkie era of cinema, <i>Archangel</i> is perhaps the only film fictionalizing the Bolshevik Revolution that features an invasion of bunny rabbits.</p>
