          <h1>THURSDAY 1 - Keanu and Nic’s Excellent Adventure</h1>
          <h3>Programmed by: Ciara Cronin, Michael Martinez, and Rachel Vigil</h3>

            <p>Keanu Reeves and Nicolas Cage occupy unique positions in American hearts and cinema. Their public images have been heavily influenced by meme culture resulting in “Sad Keanu” and a string of humorous references about Nic Cage. In many ways they have transcended their screen images and the celebrity that results, and instead have become points of cultural phenomena. In this series, we hope to return them to their screen selves: the good, the bad, and the iconic. This series contains some of their most critically lauded and more critically panned roles, but it maintains an entertaining and fun edge throughout. Watch these two modern stars with diverse careers weave their way through rich roles throughout different parts of their lives. Cage has turns as a romantic lead (MOONSTRUCK) and a campy action star (FACE/OFF), while Reeves shows off his fighting chops (JOHN WICK, SPEED, THE MATRIX). On top of that, this series offers our audiences the special opportunity to see Reeves at our very own university in CHAIN REACTION, and to imagine whether he would be a good professor or a bad one. Spanning time and genre, this series has a movie for every type of moviegoer. Though inspired by their social and cultural impact, which has permeated much of our generation, this series aims instead to appreciate and enjoy them as actors first and Keanu Reeves and Nic Cage second.</p>
