          <h1>THURSDAY 2 - No Love in Your Violence: A Takashi Miike Retrospective</h1>
          <h3>Programmed by: Eric-Antonio Guzman</h3>

            <p>A father getting a broom shoved up his butt. A sadist cutting off his tongue as penitence. A sumo wrestler smashing his girlfriend to death, figuratively and literally. These are just some of the sights you can expect to see in this series dedicated to the one and only Takashi Miike. Known for his extremely graphic and shocking images, Miike has been continuously pushing the boundaries of taste and decency throughout his prolific career.</p>

            <p>Since his early days making direct-to-video films in 1990s Japan, Miike has been directing with seemingly reckless abandon in both his output and content. From 1998–2003 alone, he directed an average of 5 films per year, including the road drama <i>The Bird People in China</i> [1998] and the controversial splatter-fest <i>Ichi the Killer</i> [2001]. With over 100 directorial credits to his name, Miike is the embodiment of what author Anthony Williams calls a “cinema of excess and outrage.”</p>

            <p>And yet, underneath the shock and gore, Miike’s films tackle deeply personal issues, like the marginalization of immigrants in <i>Ley Lines</i> [1999], and the dangers of childhood violence in <i>Dead or Alive 2: Birds</i> [2000]. His films are certainly extremely violent and taboo, but they are also extremely political and human. And it is in that spirit that we present the very best of Miike through films that are as much about their excess and outrage as they are about us.</p>
