          <img src="/images/2022winter/hiroshima-mon-amour-1959.jpg" alt="Hiroshima Mon Amour (1959) still">
          <h2>Hiroshima Mon Amour (1959)</h2>
          <h3>Alain Resnais &middot; 92m &middot; DCP</h3>
          <p>A love affair between a French actress (Emanuelle Riva) and a Japanese architect (Eiji Okada) is haunted by memories of World War II, rendered through startling jump cuts and a disembodied voiceover. A defining moment of the Nouvelle Vague, Eric Rohmer famously called it “the most important film since the war, the first modern film of sound cinema.” Hot off the success of <i>Moderato Contabile</i>, Duras reportedly wrote the script in less than two months.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Sunday, February 6th</h1>
          <img src="/images/2022winter/une-aussi-longue-absence-1961.jpg" alt="Une aussi longue absence (1961) still">
          <h2>Une aussi longue absence (1961)</h2>
          <h3>Henri Colpi &middot; 98m &middot; 35mm</h3>
          <p>Alida Valli stars in this Palme d'Or winner as a café owner plagued with grief, struggling to restore the memories of an amnesiac who might be her long-lost husband. Duras' script inverts the structure of the central relationship of <i>Hiroshima Mon Amour</i> and prefigures the haunting memory games of Christian Petzold's <i>Phoenix</i>. Henri Colpi, editor of Resnais' first two features and Duras' <i>Détruire, dit-elle</i>, gracefully handles the widescreen format.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Sunday, February 13th</h1>
          <img src="/images/2022winter/dtruire-ditelle-1969.jpg" alt="Détruire, dit-elle (1969) still">
          <h2>Détruire, dit-elle (1969)</h2>
          <h3>Marguerite Duras &middot; 100m &middot; 35mm</h3>
          <p>After weathering other directors' lackluster attempts to film her stories, Duras’ first self-adaptation accentuates the aimlessness of her novel about two couples at a seaside resort. When asked about one of the searing long takes that populates the film, Duras said: "You can imagine how hard I had to fight to keep this scene this long. Many people said to me: 'It's impossible; they exchange nothing but banalities.' But that's exactly the point."</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Sunday, February 20th</h1>
          <img src="/images/2022winter/india-song-1975.jpg" alt="India Song (1975) still">
          <h2>India Song (1975)</h2>
          <h3>Marguerite Duras &middot; 120m &middot; 35mm</h3>
          <p>Anne-Marie (Delphine Seyrig), an ambassador’s wife locked away in an embassy, has a series of affairs to stave off the emptiness of colonial life in 1930s Calcutta. Regarding the characters of the film, Duras stated, “[Women] listen to their feelings. Men don’t know how to….There are many things that women do that men could not do.” Duras reportedly played Carlos d'Allesio's dreamlike score on set to draw out the languorous performances of the actors.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Sunday, February 27th</h1>
          <img src="/images/2022winter/le-camion-1977.jpg" alt="Le Camion (1977) still">
          <h2>Le Camion (1977)</h2>
          <h3>Marguerite Duras &middot; 80m &middot; 35mm</h3>
          <p>Marguerite Duras herself appears alongside Gerard Depardieu in the writer-director’s most self-assuredly minimalist film. As Duras reads through a script about a truck driver and a hitchhiker, images of a truck driving in the countryside intermittently appear onscreen. Designed to reveal the audience's desire to suspend disbelief, <i>Le Camion</i> found an unlikely champion in Pauline Kael, who called it "perfectly made: an ornery, glimmering achievement."</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Sunday, March 6th</h1>
          <img src="/images/2022winter/le-navire-night-1979.jpg" alt="Le Navire Night (1979) still">
          <h2>Le Navire Night (1979)</h2>
          <h3>Marguerite Duras &middot; 90m &middot; 16mm</h3>
          <p>Two doomed lovers communicate across a mysterious phone line installed during the German occupation of Paris—sorry, Ephron fans, but <i>Sleepless in Seattle</i> it ain’t. As the camera sinuously roams the empty city streets and occasionally visits the actors, Duras allows the viewer to contemplate the vulnerable, lyrical dialogue between the characters, voiced by herself and Benoit Jacquot, who served as the assistant director on <i>India Song</i>.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Sunday, March 13th</h1>
          <img src="/images/2022winter/agatha-et-les-lectures-illimites-1981.jpg" alt="Agatha et les lectures illimitées (1981) still">
          <h2>Agatha et les lectures illimitées (1981)</h2>
          <h3>Marguerite Duras &middot; 86m &middot; 35mm</h3>
          <p>Though never sharing the same frame, Yann Andrea and Bulle Ogier play siblings in an incestuous relationship that recalls Duras’ early semi-autobiographical novel <i>The Sea Wall</i>. <i>Agatha</i> represents the zenith of her minimalism. Duras insisted, “The reality reproduced by classical cinema has never been of any interest to me. Everything is said too much, shown too much-an excess of meaning in which, paradoxically, the context becomes impoverished.”</p>
