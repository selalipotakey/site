          <h1>SUNDAY - Destroy, She Said: A Marguerite Duras Retrospective</h1>
          <h3>Programmed by: Zachary Vanes</h3>

            <p>"What appears in my films is the language of women, the action of women. The men are forced to follow.... It’s already the beginning of an inverted world.” – Marguerite Duras</p>

            <p> </p>

            <p>Unlike her contemporaries in post-1968 cinema, such as Agnès Varda, Chantal Akerman, and Nelly Kaplan, Marguerite Duras was already a successful novelist with a keen interest in deconstructing her own writing on-screen when she made her directorial debut. Following her monumental first screenplay for <i>Hiroshima Mon Amour</i> (1959), she worked for ten years as a screenwriter on film adaptations of her own novels directed by the likes of Jules Dassin, Tony Richardson, and Peter Brook. At the age of fifty-five, Duras directed her first feature <i>Détruire, dit-elle</i> (1969), and she continued to develop an inimitable style over the next twenty-five years of filmmaking. As a writer/director interested in exploring the intersections of colonialism, female sexuality, and the language of film itself, her influence can be felt within the work of contemporary filmmakers like Claire Denis and Nina Menkes. Denis herself has referred to Duras as an "intellectual hero."</p>

            <p> </p>

            <p><i>Destroy, She Said</i> perfectly expresses Duras' passage from a begrudging reliance on the filmic representations of others to the active expression of her personal mythologies and political concerns on screen. While she expertly elicited memorable performances from actors such as Jeanne Moreau, Delphine Seyrig, Michael Lonsdale, and Gerard Depardieu, it is undoubtedly Duras' unfettered use of her own voice that is her most thrilling challenge to the history of cinema.</p>
        </div>
        
