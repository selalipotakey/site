          <h1>THURSDAY 2 - Blow Up My Video: Movies Shot on Video, Shown on Film</h1>
          <h3>Programmed by: Zachary Vanes and Brian McKendry</h3>

            <p>At the beginning of the new millennium, the most daring (and certainly the thriftiest) independent filmmakers opted to shoot their features and shorts on video. The movies of this era blended the rigorous aesthetics of Dogme95 with Hollywood genres, reality TV, and observational documentary. While approaching untouchable subjects, filmmakers used video to create moments of comedy, horror, and beauty that were overwhelming in their rawness and veracity. However, when they appeared in theaters they did so in a hybrid format—not-quite-film and not-quite-video.</p>

            <p>Despite being shot on DV tape, three-quarter inch, and other standard definition formats, the movies included in this series were created with the expectation that they would be distributed and shown on celluloid film. The video to film or “film-out” transfer process was accomplished by a variety of technologies, the most common being the CRT recorder, which essentially involved a camera pointed at a television display monitor. Video projectors certainly existed, but movie theaters remained committed to celluloid exhibition (and unable or unwilling to invest in a completely new system).</p>

            <p> While all this sounds strange in an era when movies are regularly shot on film and transferred to digital formats in order to have widespread distribution, this series intends to provide a window into that not-so-long-ago moment when the existing infrastructure demanded the opposite. It also offers a chance to appreciate some of the most influential films of the past twenty-five years in their original transmedia context.</p>
