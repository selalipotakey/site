          <img src="/images/2021fall/inside-man-2006.jpg" alt="Inside Man (2006) still">
          <h2>Inside Man (2006)</h2>
          <h3>Spike Lee &middot; 129m &middot; 35mm</h3>
          <p>Featuring Denzel Washington, Clive Owen, Jodie Foster, Christopher Plummer, Willem Dafoe, and Chiwetel Ejiofor, Spike Lee’s contribution to the heist genre is a taut, energetic, and cleverly crafted thriller. Washington stars as Detective Keith Frazier, who is assigned to negotiate a hostage crisis during a Manhattan bank robbery. Frazier faces off with Dalton Russel (Owen), the mastermind behind the heist, in a twisting dance riddled with red herrings.</p>
