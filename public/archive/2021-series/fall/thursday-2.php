          <img src="/images/2021fall/oceans-11-2001.jpg" alt="Ocean's 11 (2001) still">
          <h2>Ocean's 11 (2001)</h2>
          <h3>Steven Soderbergh &middot; 116m &middot; 35mm</h3>
          <p>Stephen Soderbergh’s warm, thrilling, and hopelessly funny 2001 remake of the 1960 original, this is a film that in Soderbergh’s words “has no desire except to give you pleasure from beginning to end.” The epitome of the cable TV heist, <i>Oceans 11</i> makes good on its director's promise, following Danny Ocean (George Clooney) as he assembles a team of criminals to rob three casinos in Las Vegas—a plot rife with greed, adrenaline, and suave revenge.</p>
        </div>
        
        <div class="screening">
          <h1>9:30PM Thursday, October 7th</h1>
          <img src="/images/2021fall/bottle-rocket-1996.png" alt="Bottle Rocket (1996) still">
          <h2>Bottle Rocket (1996)</h2>
          <h3>Wes Anderson &middot; 91m &middot; 35mm</h3>
          <p>The first feature-length film for the decisively idiosyncratic director Wes Anderson, <i>Bottle Rocket</i> originated as a black and white 13-minute short written by Anderson and Owen Wilson, starring Owen and his brother Luke. Anderson’s visual style can be seen in its budding stages in this hilarious, dream-like tale of two best friends who stage a convoluted robbery of a bookstore, then escape “on the lam” to pursue an idealized life of crime.</p>
        </div>
        
        <div class="screening">
          <h1>9:30PM Thursday, October 14th</h1>
          <img src="/images/2021fall/the-asphalt-jungle-1950.jpg" alt="The Asphalt Jungle (1950) still">
          <h2>The Asphalt Jungle (1950)</h2>
          <h3>John Huston &middot; 112m &middot; DCP</h3>
          <p>This 1950 noir is considered the first ever heist film, catapulting the genre into cultural prominence and establishing many of its defining tropes. Just released from prison, Erwin “Doc” Reidenschneider hatches a plot to steal a million dollars' worth of jewels, recruiting a safe-cracker, driver, and heavy. The heist goes mostly to plan, but the aftermath collapses into deception and greed. Featuring Marilyn Monroe in her breakout role as Angela Phinlay.</p>
        </div>
        
        <div class="screening">
          <h1>9:30PM Thursday, October 21st</h1>
          <img src="/images/2021fall/rififi-1955.jpg" alt="Rififi (1955) still">
          <h2>Rififi (1955)</h2>
          <h3>Jules Dassin &middot; 118m &middot; DCP</h3>
          <p>Jules Dassin’s first film after being blacklisted out of Hollywood in 1949, and what is considered his masterpiece,  <i>Rififi</i> has everything: suspense, surprise, grit, and dark humor, making it one of the most influential films for the heist genre. This twisting film which has been called “the greatest of all film noirs," follows four ex-cons who execute one last plot in Paris, centering around a 30-minute silent and excruciatingly tense heist sequence.</p>
        </div>
        
        <div class="screening">
          <h1>9:30PM Thursday, November 4th</h1>
          <img src="/images/2021fall/the-italian-job-1969.jpg" alt="The Italian Job (1969) still">
          <h2>The Italian Job (1969)</h2>
          <h3>Peter Collinson &middot; 99m &middot; DCP</h3>
          <p>The penultimate representative of the comedic British caper, <i>The Italian Job</i> is an irresistibly fun and exciting heist, stuffed to the gills with Mini Coopers, a charming young Michael Caine, and a killer soundtrack by Quincy Jones. A plan is orchestrated to steal $4 million in gold bullion from the city of Turin, using a traffic jam as a means of robbery and escape. The 2003 version, a staple in the American living room, was an homage to this original.</p>
        </div>
        
        <div class="screening">
          <h1>9:30PM Thursday, November 11th</h1>
          <img src="/images/2021fall/the-taking-of-pelham-1-2-3-1974.jpg" alt="The Taking of Pelham 1 2 3 (1974) still">
          <h2>The Taking of Pelham 1 2 3 (1974)</h2>
          <h3>Joseph Sargent &middot; 104m &middot; Digital</h3>
          <p>A wry subway takeover, mixing grinding tension with witty satire that is sure to make any New Yorker’s cheeks flush. Complete with submachine guns, glasses, and one incredible score. Joseph Sargent’s inventive heist follows the capture of a downtown 6 train by a criminal gang led by Mr. Blue. They take 18 hostages and demand a million dollars within the hour. The color-coded aliases were borrowed by Quentin Tarantino for <i>Reservoir Dogs</i>.</p>
        </div>
        
        <div class="screening">
          <h1>9:30PM Thursday, November 18th</h1>
          <img src="/images/2021fall/the-killing-1956.jpg" alt="The Killing (1956) still">
          <h2>The Killing (1956)</h2>
          <h3>Stanley Kubrick &middot; 84m &middot; Digital</h3>
          <p>Stanley Kubrick’s self-proclaimed first “mature feature” is a twisting noir of a racetrack robbery. Kubrick recruited pulp novelist Jim Thompson to construct this heist which leaps through time and space, punctuated by many of Kubrick’s visual signatures. Johnny Clay (Sterling Hayden) recruits a five man team to stage a two-million dollar robbery of the Lansdowne Racetrack, a final big score before he weds his fiancée Fay (Coleen Gray) and goes straight.</p>
        </div>
        
