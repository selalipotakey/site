          <h1>THURSDAY 2 - The Evolution of the Heist</h1>
          <h3>Programmed by: Ian Resnick</h3>

            <p>Film nostalgia for me comes in the form of early 2000s heist movies watched on the couch past 9 o’clock on cable TV with kernels of popcorn stuck in my teeth and Sour Patch Kids staining my tongue an inflamed red. <i>The Italian Job</i>, <i>Rififi</i>, and the <i>Ocean’s</i> movies, and other favorites transported me to a world of smooth-talking con artists, pulsing soundtracks, and unbeatable odds inevitably beaten. This series pulls back the curtain of the late-night cable TV staple, exploring its origins in American and French Noir, altered through British comedic capers and gritty Hong Kong bloodbaths. The lineup reveals the diversity and fluidity of the genre, stretched by directors to reach new aesthetic, comedic, and narrative heights. The disjunction of Stanley Kubrick’s <i>The Killing</i>, the biting wit and intensity of Joseph Sargent’s <i>The Taking of Pelham 1 2 3</i>, the divergent aesthetics of Wes Anderson’s <i>Bottle Rocket</i>. The heist has provided a unique platform for the adventurous director, an open door for complexity and ingenuity in style, character, narrative, and tone, suggestive of the unwieldy contemporary heists we all know and love. Whether you’re here for the driving scores, ridiculous schemes, dry-as-bone humor, or the nostalgia, these heists are sure to make your heart race and your palms sweat. </p>
        </div>
        
