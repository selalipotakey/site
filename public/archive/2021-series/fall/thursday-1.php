          <img src="/images/2021fall/pulp-fiction-1994.jpg" alt="Pulp Fiction (1994) still">
          <h2>Pulp Fiction (1994)</h2>
          <h3>Quentin Tarantino &middot; 154m &middot; 35mm</h3>
          <p><i>Pulp Fiction</i> needs little introduction. The epitome of a 'dudes rock' movie, this self-aware celebration of grisly violence over a surf rock-soaked L.A. set the bar for all indie movies to follow. Employing a non-linear storyline and endless references to classic Americana, director Quentin Tarantino tells the stories of three men caught in a life of crime. Acclaimed by critics and film bros alike, this movie is better than Buddy Holly's $5 shake.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Thursday, October 7th</h1>
          <img src="/images/2021fall/mulholland-drive-2001.jpg" alt="Mulholland Drive (2001) still">
          <h2>Mulholland Drive (2001)</h2>
          <h3>David Lynch &middot; 147m &middot; DCP</h3>
          <p>Starring Naomi Watts as Betty, a naive girl trying to make it in Hollywood, and Laura Harring as Rita, an amnesiac working to puzzle out her past, David Lynch's neo-noir film <i>Mulholland Drive</i> weaves its way through a dreamlike City of Angels. Edward Guthmann of the <i>San Francisco Chronicle</i> called it "exhilarating for its fierce, frequently reckless imagination, it holds us, spellbound and amused, for all of its loony and luscious 146 minutes."</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Thursday, October 14th</h1>
          <img src="/images/2021fall/paprika-2006.jpg" alt="Paprika (2006) still">
          <h2>Paprika (2006)</h2>
          <h3>Satoshi Kon &middot; 90m &middot; 35mm</h3>
          <p>A major inspiration for Christopher Nolan's <i>Inception</i>, Satoshi Kon’s <i>Paprika</i> is a mind-bending sci-fi thriller that follows a psychologist’s chase to recover "DC Minis," devices that allow the user to enter the dreams of others. With a mix of 3D and hand-drawn animation, <i>Paprika</i>’s stunning carnival of hallucinations and dreams explores the relationship between man and machine, the subconscious world, and the darkness of modern society.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Thursday, October 21st</h1>
          <img src="/images/2021fall/in-a-lonely-place-1950.jpg" alt="In a Lonely Place (1950) still">
          <h2>In a Lonely Place (1950)</h2>
          <h3>Nicholas Ray &middot; 94m &middot; 35mm</h3>
          <p>Based on Dorothy B. Hughes' novel of the same name, this 1950 classic stars Humphrey Bogart as Dixon Steele, a short-tempered, alcoholic screenwriter in desperate need of an alibi. When aspiring actress Laurel Gray steps in to provide one, the troubled pair fall fast. But doubts begin to creep in as the lovers' true natures are revealed in what is described by critic Kim Morgan as "one of the most heartbreaking love stories ever committed to film."</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Thursday, October 28th</h1>
          <img src="/images/2021fall/the-shining-1980.jpg" alt="The Shining (1980) still">
          <h2>The Shining (1980)</h2>
          <h3>Stanley Kubrick &middot; 146m &middot; 35mm</h3>
          <p>What hasn't been said about Stanley Kubrick's genre-defining classic? This Stephen King adaptation follows Jack Torrance (Jack Nicholson), his wife Wendy (Shelly Duvall), and their young son Danny during an extended off-season stay at the historic and isolated Overlook Hotel. Cryptic, provocative, and hypnotic, Kubrick's antepenultimate flick boasts some of the most celebrated moments and imagery in horror history. <i>The Shining</i> is simply a must-see.</p>
        </div>
        
        <div class="screening">
          <h1>9:30PM Thursday, October 28th</h1>
          <img src="/images/2021fall/little-shop-of-horros-1986.jpg" alt="Little Shop of Horrors (1986) still">
          <h2>Little Shop of Horrors (1986)</h2>
          <h3>Frank Oz &middot; 94m &middot; 35mm</h3>
          <p>A remix of Roger Corman’s B-movie of the same name, Frank Oz’s <i>Little Shop of Horrors</i> accelerates the story’s tempo with catchy musical numbers from Alan Menken. Rick Moranis stars as Seymour Krelborn, a nerdy florist who gets mixed up with the wrong carnivorous plant. Filled with romance, dentistry, and ethically dubious floristry, this film is, as put by Janet Maslin in <i>The New York Times</i>, "a full-blown movie musical, and quite a winning one."</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Thursday, November 4th</h1>
          <img src="/images/2021fall/playtime-1967.jpg" alt="Playtime (1967) still">
          <h2>Playtime (1967)</h2>
          <h3>Jacques Tati &middot; 155m &middot; DCP</h3>
          <p><i>Playtime</i> is a 1967 French comedy film that loosely follows the lovable Monsieur Hulot through the futuristic workplaces and restaurants of Paris. Played by actor-director Jacques Tati, the old-fashioned Hulot serves to poke fun at a France that tries to take itself much too seriously. With frequent use of stunning 70mm wide shots and a famously enormous set, the film stands as an aesthetic triumph and lighthearted glimpse into modern life.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Thursday, November 11th</h1>
          <img src="/images/2021fall/straw-dogs-1971.jpg" alt="Straw Dogs (1971) still">
          <h2>Straw Dogs (1971)</h2>
          <h3>Sam Peckinpah &middot; 117m &middot; Digital</h3>
          <p>The ‘straw dog’ of the <i>Tao Te Ching</i> denotes something of great ceremonial worth, which is then indifferently discarded. Whether you find it an intimate examination of human savagery, or a chauvinistic story of defending house and home (or both), Sam Peckinpah’s bloody 1971 film is at the very least psychologically disturbing. Dustin Hoffman and Susan George star as the Sumner family, hiding in the Cornish moorland from the anti-Vietnam War protests.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Thursday, November 18th</h1>
          <img src="/images/2021fall/tokyo-drifter-1966.jpg" alt="Tokyo Drifter (1966) still">
          <h2>Tokyo Drifter (1966)</h2>
          <h3>Seijun Suzuki &middot; 82m &middot; DCP</h3>
          <p>A jazzy, almost gaudy gangster movie, <i>Tokyo Drifter</i> follows “Phoenix” Tetsu (Tetsuya Watari), the reformed right-hand man of a former yakuza boss, as he rejects an offer from a rival gang and is forced to flee Tokyo. Marked by the set's gorgeously lurid color palette, Suzuki presents over-the-top fight scenes interspersed with nightclub vignettes, weaving a tale of loyalty and betrayal that leaves the story's former hitman completely adrift.</p>
        </div>
        
        <div class="screening">
          <h1>7:00PM Thursday, December 2nd</h1>
          <img src="/images/2021fall/journey-to-italy-1954.jpg" alt="Journey to Italy (1954) still">
          <h2>Journey to Italy (1954)</h2>
          <h3>Roberto Rossellini &middot; 97m &middot; DCP</h3>
          <p>Considered a commercial and critical failure when first released, Rossellini's <i>Journey to Italy</i> now stands as one of his greatest films, being hugely influential in New Wave cinema. With iconic performances by Ingrid Bergman and George Sanders, the film follows a couple struggling to save their marriage. Set in Naples, the historic and beautiful Italian countryside provides a backdrop to a story concerned with mortality, romance, and the aging of love.</p>
