          <h2>Fantastic Planet (1973)</h2>
          <h3>René Laloux &middot; 72m &middot; DCP</h3>
          <p>Made in 1973 at the height of psychedelia, <i>Fantastic Planet</i> tells the story of a humanity domesticated by giant blue humanoids called "Draags." Captured as an infant, Terr is pampered and tormented by his adolescent keeper until his discovery of the Draags' transcendental knowledge casts him into the mystical world beyond. This kaleidoscopic blend of sci-fi and pop art visuals comes with a soundtrack that is a spacy mellotron wah-wah lover's dream.</p>
