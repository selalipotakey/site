          <h1>SATURDAY - Psychedelic Children’s Films</h1>
          <h3>Programmed by: Amélie Pavel</h3>

            <p>This series explores psychedelic children's films from the early 20th century to now. These films feature an emphasis on sumptuous and colorful visuals coupled with the breaking of the fourth wall that produce an expansion of the consciousness through poetic distancing of the viewer. Parallel to the viewer's distancing is the disorientation of the main character: in these films, an important theme is being transported to another foreign land or place, where space and time work differently and pressure the main character into reevaluating their belief systems and understanding of the world. </p>

            <p>Among these titles are the cult classics THE WIZARD OF OZ, ALICE IN WONDERLAND, THE NEVERENDING STORY, and THE PRINCESS BRIDE. These are the films that shaped our collective imagination, that, upon reviewing, transport us to another time and place, another dimension of our consciousness: our childhood. Other, lesser known titles make up this series as well: FANTASTIC PLANET, KIRIKOU AND THE SORCERESS, THE POINT. These films are made for children, but can very much be enjoyed by adults. </p>
