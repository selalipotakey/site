          <p>Rob Reiner's infamous cult classic <i>The Princess Bride</i> tells a thrilling and tender story of romance, adventure, and vengeance. Framed as a grandfather telling his bedridden grandson a story, the film focuses on farmhand Westley (Cary Elwes in his most beloved role) and Princess Buttercup (the ethereal Robin Wright), weaving together an unforgettable cast of heroic bandits, verbose outlaws, and odious royals in the fictional kingdom of Florin.</p>
