          <h2>The Point (1971)</h2>
          <h3>Fred Wolf &middot; 74m &middot; 16mm</h3>
          <p><i>Rare 16mm print!</i> This legendary animated film, narrated by Ringo Starr and written to accompany Harry Nilsson's eponymous 1971 album, tells the story of round-headed Oblio, who discovers that even in the fantastical kingdom of pointed heads, his life is far from pointless. Both the film and the album were inspired by a revelatory LSD trip, and the aesthetics can attest to this. Despite its cult following, the film has never had a theatrical release.</p>
