          <h3>Victor Fleming &middot; 102m &middot; 35mm</h3>
          <p>Starring sixteen-year-old Judy Garland, the 1939 adaption of L. Frank Baum's <i>The Wizard of Oz</i> follows Dorothy and her dog Toto as they are swept away from their Kansas home and into the magical land of Oz. The subject of countless cultural treatises and simulacra, nothing will ever eclipse the original tale of flying spider monkeys, the Wicked Witch of the West, and the man behind the silver screen. Follow us on the yellow brick road to Emerald City!</p>
