        <h1>Getting Involved</h1>
        <p>Doc is exclusively run by volunteers who manage every aspect from programming to projecting. Get involved with Doc by programming a film series or by becoming a volunteer in one of the capacities outlined below.</p>
        <br>
        <p>If you're interested, please contact our Volunteer Chair at volunteer@docfilms.org.</p>
        
        <br><br>

        <h1>How to program a series</h1>
        <p>Doc Films does not impose many restrictions on its programming. Anyone can propose a film series. Series may highlight the work of a particular director, star, screenwriter, cinematographer, or any other craftsperson worthy of a retrospective. Series may also be organized around genres, periods, national cinemas, political ideologies, or more abstract themes.</p>
        <br>
        <p>As a general rule, films can only be shown once as part of a weekday series within a four year period. Every film series proposal must consist of nine titles in addition to three alternates should any of the other titles prove unavailable. Any film series proposal must include film distribution as well as an accurate cost estimate for renting and shipping.</p>
        <br>
        <p>If you are interested in the programming at Doc, please subscribe to our programming mailing list <a href="">here</a>. Contact our Programming Chair at programming@docfilms.org if you have any questions.</p>
        <br>
        <p>At our weekly programming meetings we discuss potential series for the next calendar. One need not be a Doc volunteer or affiliated with the University to take part in this dialogue, or to propose a series.</p>
        <br>   
        <p><b>Programming meeting times for Autumn 2022 are TBD.</b></p>

        <br><br>

        <h1>How to become a volunteer</h1>
        <p>Anyone can volunteer at Doc Films, though priority will be given to University of Chicago students. Volunteers are asked to commit to working one screening per week for the duration of the quarter in one of the capacities listed below. In exchange, each volunteer receives a pass that admits them and a guest to every Doc Films screening that quarter. We do not ask that volunteers spend much time at Doc, but it is very important that they respect whatever commitment they do make.</p>
        <br>
        <p>Volunteer applications are sent out a few weeks before the beginning of every quarter. Contact our Volunteer Chair(s) at volunteer@docfilms.org for more information, or subscribe to our volunteering mailing list.</p>
        <br>
        <p>At our weekly volunteer meetings, board members report on and discuss their work managing Doc. All volunteers who want to get more involved or just want to learn more about how Doc is run are welcome and encouraged to attend. Volunteer meetings generally take place every Sunday at 4:30 pm in the cinema lobby.</p>
        <br>
        <p><b>General meeting times for Autumn 2022 are TBD.</b></p>
        <br>

        <h3>A short summary of roles:</h3>
        <p><u>Ticket sellers</u> are in charge of selling tickets before the movie. This is probably the easiest volunteer position and requires the smallest time commitment, about 45 minutes a week. Ticket sellers should be at Doc and ready to sell tickets at least thirty minutes before the beginning of the show, and are needed until 15 minutes after the show has begun.</p>
        <br>
        <p><u>Fireguards</u> are in charge of directing the audience to an emergency exit in the event of a fire. Also, fireguards are in charge of taking tickets, cleaning the cinema after a movie, and preventing serious fire hazards, e.g. empty fire extinguishers and people sitting in the aisles. They help to ensure first-rate show quality by acting as the projectionist's "eyes and ears" in the auditorium and alerting the booth about eventual problems. They should arrive 45 minutes before their assigned show and are needed until a few minutes after the show has ended.</p>
        <br>
        <p><u>Apprentice projectionists</u> help the projectionists with their job while learning to do it themselves. A volunteer must have two quarters of other Doc experience before being eligible for an apprentice projectionist position. We require that all of our projectionists have at least three quarters of experience as an apprentice with both 16mm and 35mm film and to pass both a written and demonstrative exam before becoming a full projectionist.</p>
        <br>
        <p><u>Projectionists</u> are responsible for the projection of the movie. Projectionists must be highly competent in the use of 35mm and 16mm projectors as well as digital cinema formats. They must be able to deal with emergencies - broken equipment or damaged film - quickly and confidently. Projection is one of the most important jobs at Doc, and should not be taken lightly.</p>
        <br>
        <p><u>Show captains</u> are responsible for leading the showtime team and making sure shows run smoothly. They coordinate with the projectionists, the ticket sellers, and the fireguards, make an introductory speech for the show, and record show statistics for later use. The show captain must arrive at Doc 45 minutes before the show to open the theater, and can't leave until after they have deposited the money, usually twenty minutes after the screening has begun.</p>
        <br>
        <p><u>Pösternauts</u> are guerrilla advertisers, responsible for the weekly distribution of flyers around campus. The 'nauts must follow their specialized routes with tape, publicity, and enthusiasm, ensuring that Doc is effectively advertised in popular and/or visible locations. Furthermore, the Pösternauts are expected to remove outdated flyers at every chance, show courtesy to other enterprising organizations, and economize their given materials throughout each quarter.</p>
        <br>
        <p><u>Design team</u> members are responsible for Doc's aesthetic presence on campus and online via flyers, banners, and social media tiles. Members can choose which films they wish to design materials for at the beginning of the quarter, and these are then distributed by the Pösternauts and Publicity Chair. Please contact the Design Chair at design@docfilms.org.</p>
