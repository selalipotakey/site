          <h1>The Documentary Film Group</h1>
          <p>It comes as a shock to no one that Doc Films is older than 85 countries; we have all the hallmarks of an aged nation, like a murky origin story, a hoarded heap of ill-gotten relics, and many rocky (though thankfully nonviolent) transitions of power. It’s hard to sum up 90 years of history in just a few paragraphs, but even if we wanted to, what we know about Doc is more tall tale than recorded fact. Instead, what we can give you is a look into the Booth, into the Board, and into the Doc archives—into the story, be it real or apocryphal, behind the screen in the Max P. Cinema. </p>

          <p>When Doc began in 1932 (or 1941, or 1933, depending on whom you ask), it was comprised of a bunch of students interested in the “neglected application of the cinema arts [...] known as the Documentary Film.” But, after realizing that profit also matters somewhat, programming gradually expanded to include popular films, fiction, and narrative. Along the way, we clashed with—and sometimes absorbed—other film groups on campus, such as Law School Films and Contemporary European Films. In the ‘60s, Doc strode to the forefront of the American auteur era, inviting directors as varied as Hitchcock and Brakhage to the theater, and even got into a minor squabble with Roger Ebert about our status as the oldest film society in Illinois (yes, we won—we’re the oldest in the country). A calendar from the ‘70s proudly proclaims, “Still only $6!” as advertisement for a quarter pass, and in these years Doc spawned a variety of film critique magazines, including Voyeur and Focus!. In the ‘80s, Doc moved to its current home in the Max Palevsky Cinema, and in the new millennium we’ve held steady as staunch appreciators of celluloid in a digital era.</p> 

          <p>These are some of the more well-recorded things that we know about Doc. Conversely, the bits in between are the parts of Doc memory which refuse to be captured in words, and instead live on as legends passed from person to person. Whether accosted at an RSO fair, pulled in by a scheming friend, indoctrinated after a screening, or coaxed into the projection booth not by promises of money or fame but by the allure of a rare celluloid print, the volunteers who stumble into Doc become the unwitting guardians of its history. In fragments, we receive bits and pieces—some common knowledge, like the names, Wanda and Evelyn, of the twin 35mm projectors that have faithfully served Doc for decades, and some lesser known, like the fact that the projectors came to us from an orphanage from the 1950s. In our opinion, it’s the puzzle of our history that makes Doc so interesting. How did a bunch of college students start a movie theater? How did a bunch of college students <i>keep</i> a movie theater? Our guess is that it’s been a combination of miracles and hard work. On our 90th anniversary, we’re here to celebrate whatever it is that happened, and to hopefully kick off another 90 years of Doc. </p> 
