        <div class="screening">
            <h1>6:00PM October 1st</h1>
            <img src="/images/2022fall/light-2018.png" alt="light-2018">
            <h2>Light 光 (2018)</h2>
            <h3>Tsai Ming-Liang &middot; 18min &middot; DCP</h3>
            <p><i>Light</i> captures changes in the natural light streaming through Taipei's Zhongshan Hall. Zhongshan Hall has national significance as the site where Japanese forces in Taiwan formally surrendered at the end of WWII, ending 51 years of Japanese occupation. It also bears personal significance for Tsai: it is the place where he volunteered, won a filmmaking award, ran a café, held screenings of classic films, and shot his feature-length film <i>Your Face</i>.</p>
            <br>
            <p>Director’s Statement:</p>
            <p><i>“The very first Taipei Golden Horse Film Festival was held here at Zhongshan Hall. During my university days, I volunteered as a ticket seller in order to watch films for free. Many years later, I received the top award at the Taipei Film Festival in an award ceremony held here as well. I have also run a coffeehouse here and often held small screenings of classic films during that time. Last year, I shot my film, </i>Your Face<i>, inside Guangfu Auditorium. The film was composed of thirteen big close-ups. Each of those thirteen faces was filled with the passage of time. Now, I am given a chance to film Zhongshan Hall again. I switched off all the lights and allowed the warm winter sun to shine on her face.”</i></p>
        </div>

        <div class="screening">
          <h1>6:00PM October 1st (following LIGHT)</h1>
          <img src="/images/2022fall/your-face-2018.jpg" alt="image">
          <h2>Your Face 你的臉 (2018)</h2>
          <h3>Tsai Ming-Liang &middot; 76min &middot; DCP</h3>
          <p><i>Your</i> Face is composed of thirteen portraits of citizens of Taipei. Some remain silent while others offer life stories, confessions, and even a short tune. Twelve of the subjects were encountered by Tsai in the city streets, while the final subject is actor Lee Kang-Sheng, who has become a fixture of Tsai's filmography. <i>Your Face</i> explores the depth of lit faces, and the stories they convey. It is also Tsai's first scored film in over twenty years.</p>
          <br>
          <p>Director's Statements:</p>
          <p><i>“As I searched the streets of Taipei for faces to film, some verses began to appear in my mind. I wrote them down:</i></p>
          <br>
          <p>
            <i>
              There is some light, there is a story. <br>
              Your face tells of the passage of time and places you have journeyed. <br>
              In your eyes, there is a tinge of confusion and sadness.
            </i>
          </p>
          <br>
          <p>
            <i>
              There is some light, there is a story. <br>
              Your face tells of love and the places it hides in. <br>
              In your eyes, there is a sparkle and some darkness. <br>
            </i>
          </p>
          <br>
          <p><i>This is the meaning of the film.”</i></p>

          <br><br>

          <p><i>“Last year, I made a VR film. Since it was VR, there was no composition in the traditional sense and no close-ups as well. After I made the film, I had a sudden desire to film close-up shots. I decided to make a film made up of only close-ups. Hsiao-kang will be in it, of course, together with a few others. So, I went out to the streets in search of the faces I wanted. In two months, I found more than ten of them.</i></p>
          <p><i>One after another, I lit them with extra attention, as if they were cinematic compositions.</i></p>
          <br>
          <p><i>Not long after, I met Ryuichi Sakamoto by chance at the beach in Venice. After I pieced together those big close-ups that I had filmed, I wrote him a letter and asked if he'd have a look at it. He agreed. One month later, he sent me some audio files. I couldn't be sure what exactly I heard. Sakamoto told me politely that I could use it in any way I wanted. Or I could simply ignore it. I was thrilled by it. It felt like an adventure. After more than twenty years, I finally made a film with a musical score.”</i></p>

