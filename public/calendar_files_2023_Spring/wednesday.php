          <h1>WEDNESDAY - The Films of Ang Lee</h1>
          <h3>Programmed by: Wei</h3>

            <p>Born in 1954, Ang Lee is the oldest son of Sheng Lee, a middle school principal who moved to Taiwan following the Chinese civil war. Despite strong opposition from his father, Lee went on to study drama and film production in the United States after graduating from a three-year college in Taiwan. This estranged relationship with his traditional authoritarian father has had a huge impact on Lee’s films—most prominently in his “Father Knows Best” trilogy, which includes his debut feature film, <i>Pushing Hands</i>. Even in later films like <i>Life of Pi</i>, we can see the same individual struggle to reconcile generational tension. TAB Lee considered himself “a drifter and an outsider” throughout his life, being a mainlander in Taiwan and an immigrant in the United States. His zigzagging personal trajectory enables him to be more aware of cultural nuance and venture into the realm of East and West. Who could have imagined that the film adaptations of Eileen Chang’s<i> Lust Caution</i> and Jane Austen’s <i>Sense and Sensibility</i> were from the same director? Even within the well-defined Western film genre, his bold infusion of gay romance in <i>Brokeback Mountain</i> subverted the traditional “American dream” core, receiving both critical acclaim and commercial success. Aside from the intricate depiction of emotional turmoil and cultural exploration, Lee’s films also embrace many technical innovations, from the CGI in <i>Hulk</i> to his latest experiments with high frame rate. Lee refuses to be stereotyped, and continues to be a permanent student of film. Come to Doc and enjoy the diverse filmography within this Ang Lee retrospective!</p>
            
            <p><b>Wei Lu </b></p>
