          <h2>Johanna d'Arc of Mongolia (1989)</h2>
          <img src="/images/2023spring/johanna-darc-of-mongolia-1989.jpg" alt="Johanna d'Arc of Mongolia (1989) still">
          <h3>Ulrike Ottinger &middot; 165m &middot; 35mm</h3>
          
          <p><i>Introduced by Prof. Daniel Morgan, Cinema and Media Studies. Print courtesy of the Academy Film Archive.</i></p><p>An eclectic mix of women passengers on the Trans-Siberian railway—Lady Windermere (Seyrig), an American Broadway actor, a German teacher (Irm Hermann), and an adventurous girl—are taken captive by a hospitable Mongolian princess and experience Mongolia in all its beauty and power. Language and culture pose no boundaries in this colorful whirlwind of a film. One of Seyrig's four collaborations with director Ulrike Ottinger, and also her final film.</p>
