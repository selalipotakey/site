          <p><i>Introduced by Aurore Spiers, CMS and CSGS. Restored by the ZKM de Karlsruhe in partnership with the Centre Pompidou. Courtesy of the Centre audiovisuel Simone de Beauvoir.</i></p><p>In 1975, a French TV show ran a program on the "year of the woman." Feminist video collective Les Insoumuses responded with <i>Maso and Miso</i>, a humorous and disruptive re-edit of the misogynistic show. Followed by a documentary about Seyrig's attempts to make a film about Calamity Jane's letters to her daughter. Babette Mangolte (cinematographer, <i>Jeanne Dielman</i>) interweaves footage and other materials from the incomplete journey in an homage to Seyrig.</p>
