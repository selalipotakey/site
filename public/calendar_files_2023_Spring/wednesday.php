          <h2>Last Year at Marienbad (1961)</h2>
          <img src="/images/2023spring/last-year-at-marienbad-1961.jpg" alt="Last Year at Marienbad (1961) still">
          <h3>Alain Resnais &middot; 94m &middot; DCP</h3>
          
          <p><i>Followed by a discussion with Prof. Maria Belodubrovskaya, Cinema and Media Studies.</i></p><p>Truth, time, and memory are dizzyingly twisted and muddled in a hotel in Marienbad, where a man insists he has met an unnamed woman (Seyrig) before, but she insists she does not know him. Conflict arises with the appearance of another man—what is the truth? This earlier role of Seyrig’s would pave the way for her future castings as the elusive object of desire. The script’s puzzle is enhanced by the hypnotic, surreal cinematography of Sacha Vierny.</p>
          <h2>7:00PM Wednesday, March 22nd</h2>
        </div>
        
        <div class="screening">
          <h2>Jeanne Dielman, 23, quai du Commerce, 1080 Bruxelles (1975)</h2>
          <img src="/images/2023spring/jeanne-dielman-23-quai-du-commerce-1080-bruxelles-1975.jpg" alt="Jeanne Dielman, 23, quai du Commerce, 1080 Bruxelles (1975) still">
          <h3>Chantal Akerman &middot; 201m &middot; DCP</h3>
          <p>The film that changed everything. Delphine Seyrig gave her greatest contribution to cinema in Chantal Akerman’s <i>Jeanne Dielman</i> as a widowed housewife who spends her days peeling potatoes for her son's meals and trying to make ends meet. The film’s brilliance lies in its attentiveness towards its subject, the textural duration and repetition of Jeanne’s daily tasks, and the eventual disruption that occurs. Quietly harrowing, exhilarating, and triumphant.</p>
          <h2>7:00PM Wednesday, March 29th; 4:00PM Sunday, April 2nd</h2>
        </div>
        
        <div class="screening">
          <h2>Muriel, or the Time of Return (1963)</h2>
          <img src="/images/2023spring/muriel-or-the-time-of-return-1963.jpg" alt="Muriel, or the Time of Return (1963) still">
          <h3>Alain Resnais &middot; 116m &middot; DCP</h3>
          <p>Resnais exchanges Seyrig’s dreamy character in <i>Marienbad</i> for Hélenè, an agitated widow who lives in an antique shop with her stepson Bernard. He is deeply haunted by his time in the Algerian war, just as Hélenè is haunted by an ex-lover’s visit. Characters are trapped in their own histories as they confuse memories or obsess over them. Do they realize the futility of their perpetual forgetting and remembering? Perhaps, but they persist, or cannot escape.</p>
          <h2>7:00PM Wednesday, April 5th</h2>
        </div>
        
        <div class="screening">
          <h2>Stolen Kisses (1968)</h2>
          <img src="/images/2023spring/stolen-kisses-1968.jpg" alt="Stolen Kisses (1968) still">
          <h3>François Truffaut &middot; 90m &middot; DCP</h3>
          <p>In the third of Truffaut’s Antoine Doinel films, the ever-youthful Antoine (Jeanne Pierre-Léaud) gets a job as a private detective and works undercover at a shoe store. He becomes relatably infatuated with his boss’s wife, Fabienne Tabard (Seyrig), who seduces him with her elegance, magnetic beauty, and charm. In her famous monologue, Fabienne says to Antoine: “I am not an apparition, I am a woman,” a quote which can equally encapsulate Seyrig’s career.</p>
          <h2>7:00PM Wednesday, April 12th</h2>
        </div>
        
        <div class="screening">
          <h2>Baxter, Vera Baxter (1977)</h2>
          <img src="/images/2023spring/baxter-vera-baxter-1977.jpg" alt="Baxter, Vera Baxter (1977) still">
          <h3>Marguerite Duras &middot; 91m &middot; Digital</h3>
          <p>The film’s constant looping soundtrack accompanies a lonely Vera Baxter in an empty villa. A woman (Seyrig) hears Baxter’s name at a bar and is drawn to find her. Seyrig’s unique voice is seldom heard here; instead, she listens as Vera recounts to her the details of how her husband treats her as a commodity. Seyrig's character's presence is mysterious, never explained, yet she is our access to Vera, revealing the emptiness of her life through dialogue.</p>
          <h2>7:00PM Wednesday, April 19th</h2>
