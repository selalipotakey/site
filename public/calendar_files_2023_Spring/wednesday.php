          <h2>Letters Home (1986)</h2>
          <img src="/images/2023spring/letters-home-1986.jpg" alt="Letters Home (1986) still">
          <h3>Chantal Akerman &middot; 104m &middot; DCP</h3>
          
          <p><i>Courtesy of the Centre audiovisuel Simone de Beauvoir.</i></p><p>Known for the films about her relationship with her mother, Akerman’s third and last collaboration with Seyrig is fitting and poignant. Rarely seen, <i>Letters Home</i> is an adaptation of Rose Goldemberg's play, based on Sylvia Plath’s letters with her mother. Seyrig and her niece (Coralie Seyrig) recite the text directly to camera. Seyrig had asked Akerman to direct her in Racine's "Phèdre," but Akerman, not knowing Seyrig would die not long after, declined.</p>
