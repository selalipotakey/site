          <img src="/images/2023spring/sois-belle-et-taistoi-1981.jpg" alt="Sois belle et tais-toi! (1981) still">
          <h3>Delphine Seyrig &middot; 110m &middot; DCP</h3>
          
          <p><i>Introduced by Matthew Hubbell, Cinema and Media Studies. Courtesy of the Centre audiovisuel Simone de Beauvoir.</i></p><p><i>Be Pretty and Shut Up!</i> Delphine Seyrig turns the camera around to become the filmmaker, breaking the expectations and illusions placed on herself and others as she documents a series of interviews with well-known actresses like Juliet Berto, Jane Fonda, Shirley MacLaine, Anne Wiazemsky, and more. Seyrig asks questions that radically allow the women to speak for themselves, illuminating what it means to be a woman in the patriarchal film industry.</p>
