          <h1>WEDNESDAY - Delphine Seyrig, More Than a Muse</h1>
          <h3>Programmed by: Hannah Yang</h3>

            <p>What can one say that encapsulates Delphine Seyrig, the actress whose career spans the height of French cinema and who contributed to some of its most important works by directors like Resnais, Truffaut, and Buñuel? </p>

            <p>Her unique voice, striking features, and ability to reveal the deepest of emotions with a single glance undoubtedly shaped her early-career roles—often the elusive, intellectual woman of desire as in <i>Marienbad</i> or <i>Stolen Kisses</i>. But, sensing that “theater and films are very far from women’s consciousness about themselves,” she refused to simply be a muse. </p>

            <p>In 1975 she made three films with women and broke with this image-illusion, especially transcending it with her performance in Chantal Akerman’s <i>Jeanne Dielman</i>. Seryig stepped behind the camera and created the pioneering French feminist video collective Les Insoumuses with Carole Roussopolous, stating, “video is my independence from men.” Her work was explicitly feminist (even labeled as militant) and she fiercely advocated for issues like abortion rights. Through the end of her career, Seyrig continued to make work with women and about women, leaving the roles of her early days behind. She collaborated frequently with Marguerite Duras and Ulrike Ottinger, and held a close relationship with Chantal Akerman, who noted that the French film establishment could not tolerate “the incongruity between their fantasy of her and what she was—a total feminist activist to the end of her life.” This, combined with her unwillingness to compromise and become the perfect cinema muse, lessened what should be a significant legacy. Her contributions, both on and off screen, are invaluable. This series attempts to celebrate and bring to light her monumental life’s work as an actor, filmmaker, and woman.</p>
            
            <p><b>Co-sponsored by the France Chicago Center.</b></p>
