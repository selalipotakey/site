          <h2>Maso and Miso Go Boating (1976) // Calamity Jane & Delphine Seyrig, A Story (2020)</h2>
          <img src="/images/2023spring/maso-and-miso-go-boating-1976.jpg" alt="Maso and Miso Go Boating (1976) // Calamity Jane & Delphine Seyrig, A Story (2020) still">
