          <h1>TUESDAY - False Preachers</h1>
          <h3>Programmed by: Hannah Ozmun</h3>

            <p>In the opening sequence of <i>The Night of the Hunter</i>, Lillian Gish reads from the Gospel of Matthew as children’s faces shine like stars against the night sky: “Beware of false prophets which come to you in sheep’s clothing; but inwardly they are ravening wolves.” Taken from Jesus’s Sermon on the Mount, the text goes on to provide another metaphor for identifying false prophets: “A good tree cannot bring forth evil fruit, neither can a corrupt tree bring forth good fruit ... Wherefore by their fruits ye shall know them.” TAB This series explores the trope of the false prophet in American film, ranging from the cartoonish wickedness of Reverend Harry Powell in <i>The Night of the Hunter</i>, to the delirious atheism of Hazel Motes and sham piety of Asa Hawks in <i>Wise Blood</i>, to the tragic big-heartedness of Sonny in <i>The Apostle</i>. These films provide a window into the entanglement of money, morality, and ecstatic faith that characterizes the American religious landscape. Conscience threatens to dampen charisma as many of these preachers grapple with their sins and struggle to justify their double lives. Glimpses of sincerity shine through otherwise cynical stories of hucksters looking to line their pockets. Divine revelation overpowers human weakness, egotism, and duplicity. Blindness—both physical and figurative—appears as a motif in several of these films, suggesting that the eyes cannot always be trusted: not by sight, but “by their fruits ye shall know them.”</p>
            
            <p><b>Supported by The Martin Marty Center at the University of Chicago Divinity School.</b></p>
