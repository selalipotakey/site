          <h2>The Night of the Hunter (1955)</h2>
          <img src="/images/2023fall/the-night-of-the-hunter-1955.jpg" alt="The Night of the Hunter (1955) still">
          <h3>Charles Laughton &middot; 92m &middot; DCP</h3>
          <p>A diabolical traveling preacher marries a gullible widow, hoping to pocket the $10,000 hidden away by her late husband. Set in the Depression-era South, <i>The Night of the Hunter</i> follows her children as they flee their stepfather by starlight. Equal parts horror, comedy, and fantasy, <i>The Night of the Hunter</i> echoes tropes of silent film with striking shadowplay and an unforgettable performance by 61-year-old Lillian Gish.</p>
          <h2>7:00PM Tuesday, September 26th</h2>
        </div>
        
        <div class="screening">
          <h2>Salesman (1969)</h2>
          <img src="/images/2023fall/salesman-1969.jpg" alt="Salesman (1969) still">
          <h3>Albert, David Maysles, and Charlotte Zwerin &middot; 91m &middot; DCP</h3>
          <p>The door-to-door salesmen in this Maysles brothers documentary aren’t exactly preachers—but they are in the business of religion as they try to sell large, expensive Bibles to working-class Catholics. A portrait of mid-century consumerism and suburban ennui, <i>Salesman</i> follows its subjects from house to house as they pitch the Good Book and swap notes about the day’s successes and failures.</p>
          <h2>7:00PM Tuesday, October 3rd</h2>
        </div>
        
        <div class="screening">
          <h2>Wise Blood (1979)</h2>
          <img src="/images/2023fall/wise-blood-1979.jpg" alt="Wise Blood (1979) still">
          <h3>John Huston &middot; 106m &middot; Digital</h3>
          <p>John Huston’s <i>Wise Blood</i> captures the absurd, grotesque, yet revelatory tenor of Flannery O’Connor’s acclaimed novel. Wild-eyed Hazel Motes (Brad Dourif) becomes a self-proclaimed prophet of the Church Without Christ: “where the blind don’t see and the lame don’t walk and what’s dead stays that way." Motes battles a rival street preacher (Harry Dean Stanton) for souls - including his own.</p>
          <h2>7:00PM Tuesday, October 10th</h2>
        </div>
        
        <div class="screening">
          <h2>Body and Soul (1925)</h2>
          <img src="/images/2023fall/body-and-soul-1925.jpg" alt="Body and Soul (1925) still">
          <h3>Oscar Micheaux &middot; 102m &middot; DCP</h3>
          <p>27-year-old Paul Robeson made his acting debut in Oscar Micheaux’s <i>Body and Soul</i> playing the roles of identical twins - one honest but poor, the other a crooked preacher - who are in love with the same woman. Micheaux was forced to cut “immoral” and “sacrilegious” scenes; while the director’s cut has never been recovered, <i>Body and Soul</i> is one of Micheaux’s few surviving silent films.</p>
          <h2>7:00PM Tuesday, October 17th</h2>
        </div>
        
        <div class="screening">
          <h2>Marjoe (1972)</h2>
          <img src="/images/2023fall/marjoe-1972.jpg" alt="Marjoe (1972) still">
          <h3>Howard Smith and Sarah Kernochan &middot; 88m &middot; Digital</h3>
          <p>Marjoe Gortner started preaching in Pentecostal revivals when he was four years old. Now in his late 20’s, Marjoe doesn’t believe in God - but continues to make his living on the revival circuit. In this Academy Award winning documentary, Marjoe takes camera crews behind the scenes of Pentecostal revivals, explaining the tricks of the trade while coming clean about his double life.</p>
