          <h1>WEDNESDAY: Center Stage: The Films of Maggie Cheung</h1>
          <h3>Programmed by Deany Cheng</h3>
          
          <p><i>“For me, acting is life.” – Maggie Cheung</i>.</p>  
 
          <p>A case could be made that Maggie Cheung is the most important film actor of the last forty years, as well as our last truly global superstar. A polyglot whose prolific twenty-year filmography spans three continents, Cheung heralded a modern, cross-cultural celebrity that bridged the institutional art-house of the West with emergent cinema from Hong Kong and China. Cheung’s films often touched on themes of diaspora and Chinese identity in eras in which both were very much in flux, and through the lens of her work, we glimpse the promise and anxiety of a world about to transition to a new millennium.</p> 

          <p>By virtue of her collaborations with other greats of 90s Hong Kong cinema, both in front of and behind the camera, Cheung’s œuvre also functions as a capsule of that rich scene. This series alone features films directed by Jackie Chan, Stanley Kwan, and Johnnie To; and her co-stars run the gamut from global icons like Michelle Yeoh and Tony Leung Chiu-wai to performers like Anita Mui and Brigitte Lin—legends in their own right who never quite crossed over to the West. Cheung’s filmography, then, is also the perfect entry point to an eclectic and rewarding cinematic tradition.</p>
        </div>

