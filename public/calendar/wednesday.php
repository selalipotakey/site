          <h1>WEDNESDAY - Jean Renoir: The Grand Reality</h1>
          <h3>Programmed by: John Litweiler</h3>

            <p><i>“Jean Renoir stands on his own: the greatest of European directors: very probably the greatest of all directors.” – Orson Welles</i></p>

            <p>Jean Renoir had all kinds of stories to tell—comedies, tragedies, thrillers, satire, tales from classic literature. These stories are overflowing with life, said Martin Scorsese, "the little surprises and harmonies and beauties of character and atmosphere and light that make up experience." Most of all, Renoir gives us unforgettable human beings from all walks of life.  Their passions, their faiths, their failings move us, for as he wrote, “You see, in this world, there is one awful thing, and that is that everyone has his reasons.”</p>

            <p>A son of painter Pierre-Auguste Renoir, Jean began directing in 1921. His great period was the 1930s. By mid-decade, while grim fascist dictatorships in three bordering countries thundered toward world war, his films began reflecting anti-fascist, anti-racist, socialist ideals. After his two greatest movies were banned in occupied France, he escaped to America.</p>

            <p>The freedom Renoir allowed his actors, his love of location filming, and his missed deadlines made him unpopular with Hollywood's studios. Though he became a U.S. citizen, he returned to filming, with less success, in France in the 1950s. By then Italian and Indian realism and  France's New Wave were strongly influenced by him. Movie lovers rejoice that his masterpiece <i>The Rules Of The Game</i> (1939) is now being rereleased in theaters. In 2022, as smogs of racist, anti-democracy, anti-human rights movements threaten America, Renoir's clear-eyed realism and optimism are most welcome.</p>
