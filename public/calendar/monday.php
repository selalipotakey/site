          <h1>MONDAY - Baby Face: The Films of Barbara Stanwyck</h1>
          <h3>Programmed by: Deany Cheng</h3>

            <p>Few actors then or now have been able to put together a career as eclectic as Barbara Stanwyck’s. Growing up orphaned and starting out as a Ziegfeld girl and on Broadway before heading out west, she would become one of the great institutions of the then-nascent motion picture business, and was at one point the highest-paid star in all of Hollywood. While perhaps best-known today for her lead turns in <i>Double Indemnity</i> and <i>The Lady Eve</i>, Stanwyck was one of the most prolific and versatile stars of her time, successfully adapting her distinct, hard-boiled persona across the pre-Code, Code, and post-Code eras of Hollywood cinema. Her career is then in its way a palimpsest through which we can trace the early days of an American film industry that was still coming into its own: A dictionary for cinema before cinema had defined itself.</p>

            <p>This selection of seven lesser-seen films (many in 35mm) from her oeuvre is an attempt to capture that breadth, featuring movies from all different directors (including luminaries like Frank Capra, Douglas Sirk, and Samuel Fuller) across three decades. Covering risqué pre-Code turns (<i>Ladies of Leisure, Baby Face</i>), top-flight dramas (<i>Stella Dallas, The Strange Love of Martha Ivers, All I Desire</i>), and genre fare from the bitter to the sweet (<i>Sorry, Wrong Number, Forty Guns</i>), this series is a celebration of one of cinema’s most important and enduring figures.</p>
