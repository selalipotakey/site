          <h1>THURSDAY 2 - Skin Under Skin: A Retrospective of David Cronenberg</h1>
          <h3>Programmed by: Isaiah Terry</h3>

            <p>Within the films of David Cronenberg lies a miasmic sort of paranoia which seeps its way into your mind. It’s slow at first—barely even noticeable—and yet like an infection once it has its tendrils within your very soul it will be reticent to ever let go. To watch Cronenberg is a challenge, a deep dive into worlds of gruesome nightmares and broken people. Worlds where everything you think you know has been upended and the very laws of reality break down. It would be easy to see fans of such a director as masochists, but this would be wholly incorrect. For as terrifying, disturbing, and downright sickening as some of these movies are, there is equal amounts of humor, absurdity, and above all strange, strange sexual tension. To enjoy Cronenberg is to embrace all of these traits—to love all of these traits equally. </p>

            <p>From the exploding heads of <i>Scanners</i> to the glorious body modifications in <i>Crimes of the Future</i>, these nine films will take you on a journey through Cronenberg in the most uncomfortable way possible. Be wary, but also don’t be too afraid, each and every one of these films is worth a shot. So then, watch Cronenberg because of the practical effects. Watch Cronenberg because of his almost prophetic visions of the future. And most of all, watch Cronenberg for the ideas which shouldn’t work and yet do so with a resounding “Ew!”</p>
