          <h1>THURSDAY I: Shakespeare Remixed</h1>
          <h3>Programmed by Michelle Chow</h3>
             
          <p>Come back to school with this off-kilter selection of Shakespearean adaptations–broadly defined. Here you’ll find a host of different genres, from teen rom-coms like <i>10 Things I Hate About You</i> and <i>She’s the Man</i> and old-school sci-fi like Forbidden Planet, an influential 50s space opera which draws inspiration from “The Tempest,” to big-budget epics like Akira Kurosawa’s stunning <i>Ran</i> and moody art-house numbers like <i>My Own Private Idaho</i>. In these adaptations, minor characters sharpen under the spotlight, and new settings raise different problems and questions.</p>  
            
          <p>Some films in this eclectic mix quote the source material verbatim; others are hardly recognizable compared to the supposed original. What does it mean to create by drastically adapting, especially given how deeply canonical Shakespeare is? The vast network of adaptations that Shakespeare supports, and is supported by, trouble concepts like originality, theatricality, and ‘high’ and ‘low’ culture. As each film sculpts out its relationship to the original and to the milieu of other adaptations, they crystallize a more rich sense of themselves–of their own portrayals of power, longing, curiosity, betrayal, infatuation, despair, comedy and tragedy, silliness and petty jealousy and ennui. As many reimaginings as there are, they leave room for even more, and even weirder ones, to follow.</p>
