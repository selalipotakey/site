          <img src="/images/2024winter/storm-over-lisbon-1944.png" alt="Storm Over Lisbon (1944) still">
          <h3>George Sherman &middot; 86m &middot; DCP</h3>
          <p>A fitting end to Sherman’s seven-year tenure at low-budget studio Republic Pictures, <i>Storm Over Lisbon</i> is a bizarre knock-off of <i>Casablanca</i>. Featuring Republic regular Vera Ralston as dancer Maritza and the ever-striking Erich von Stroheim as the villain Deresco, this admittedly cheap attempt at riding the coattails of more popular fare is elevated by the cinematography of the great film noir stylist John Alton.</p>
