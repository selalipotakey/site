          <h1>MONDAY - A Brief Intro to George Sherman</h1>
          <h3>Programmed by: Hunter Koch</h3>
          <p>George Sherman is a name few may readily recall. With over one hundred directorial credits in a four decade career, Sherman represents a prolific anonymity so characteristic of film production in classical Hollywood. His legacy does little to rectify this: these films are rarely screened, and even more rarely promoted with the director’s name. Yet this series does not offer an auteurist resuscitation of a lost master. Rather, these eight films treat George Sherman as a guide through lower-budget Hollywood filmmaking in the 40s and 50s. While perhaps not “true” B-pictures—we might classify some of them as “Lazy As” or programmers—they index a production context where Hollywood excelled. Sherman’s films are short, cheap, efficient. They are also garish, tacky, and exploitative. They are true representatives of run-of-the-mill filmmaking during a period prone to hagiography. After all, film history is more than so-called “masterpieces.” This series offers an alternative set of values for repertory filmgoing. Accepting that we won’t find a lost classic opens one up to an appreciation of historical context and a greater understanding of what kinds of films were regularly made and seen. Hundreds of noirs, adventure pictures, and westerns are all but forgotten by most, yet they make up the bulk of the Hollywood corpus. In them, an idiosyncratic beauty appears in flashes on the screen; rote genre pictures give way to moments of surprising depth and eccentricity. Consider this a brief foray into a deeper consideration of Hollywood through the work of a consummate craftsman.</p>
