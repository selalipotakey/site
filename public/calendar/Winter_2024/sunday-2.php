          <h1>SUNDAY 2 - Revising the Musical</h1>
          <h3>Programmed by: Honor Torrance and Cyrus Westerlund</h3>
          <p>The era of Classical Hollywood featured a set of tried-and-true genres which would later be revised and transformed as tastes and sensibilities shifted with the changing times. The Westerns of the ’30s through the ’50s would become “Revisionist Westerns,” classic noir films would become “Revisionist Noirs” or “Neo-Noirs,” and so forth. Thinking of these genre shifts, we (Cyrus and Honor) couldn’t help but ask ourselves: what of the revisionist musical?? In the ’70s and ’80s, filmmakers began to undertake original movie musicals that redefined what the genre could be. The musical was no longer limited simply to Broadway-style whimsy: it could also be psychological, absurd, thrilling, personal, or strange. The genre became more eclectic than ever: some were genre bending (<i>Earth Girls are Easy</i>), overtly political (<i>Une Chambre en Ville</i>), contributions to iconic franchises (<i>The Muppet Movie</i>), or even creative outlets for non-Hollywood musicians (<i>True Stories</i>). In this series Revising the Musical, we have assembled a set of original musicals to encapsulate this unique era, made inside and outside of Hollywood, with several that facilitated the evolution of the genre into the following decades. This series is first and foremost a celebration of the musical that strives to illustrate the versatility of the genre…and it’s also a ton of fun!</p>
