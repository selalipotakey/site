        <h1>The Max Palevsky Cinema</h1>
        <p>Dedicated to providing a low-cost, high-quality venue for artistic, relevant and socially important domestic and international movies, Doc currently shows films at the University of Chicago's Max Palevsky Cinema, located at 1212 East 59th Street. The theater is named after Max Palevsky, whose generous gift allowed Doc to build and operate a state-of-the-art cinema.</p>
        <br>
        <p>The cinema boasts two Simplex XL 35mm projectors with Christie xenon lamphouses, one Eastman 25 16mm projector, and an NEC NC1200C digital projector. The projectors' variable speed and full aperture lens allow Doc Films to present silent films in the closest approximation to their original exhibition. The theater's Dolby Digital and Digital Theater Systems (dts) audio systems meet the highest standards in digital sound.</p>

