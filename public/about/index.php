        <h1>A Short History of Doc</h1>
        <p>The organization named 'Doc Films' was officially founded in December 1940 as the International House Documentary Film Group, though its antecedents stretch back to 1932. Initially the group focused on "the realist study of our time via nonfiction film," but the documentary alone could not sustain the organization; within a few years, the group's programs expanded to include fiction and experimental films, a mixture that it maintains to this day.</p>
        <br>
        <p>Many film directors, among them Alfred Hitchcock, Woody Allen, John Ford, and Claire Denis, have visited Doc to present films and lead discussions. Over the decades Doc has hosted an astonishing number of Chicago premieres, giving the city its first glimpse of such masterpieces as <i>The Rules of the Game</i>, <i>Au hasard Balthazar</i> and <i>Brokeback Mountain</i>.</p>
        <br>
        <p>Volunteering for Doc has led several former students to careers in filmmaking and film criticism. Former members include Ernest Callenbach, founding editor of Film Quarterly; Gordon Quinn and Gerald Temaner, co-founders of Kartemquin Films (<i>Hoop Dreams</i>, <i>Golub</i>, <i>Home for Life</i>) and filmmakers Aaron Lipstadt (<i>City Limits</i>, <i>Android</i>) and Myron Meisel (<i>It's All True: Based on an Unfinished Film by Orson Welles</i>). Most recently, Doc alums Becca Hall, Julian Antos, and Kyle Westphal founded <u><a href="https://www.chicagofilmsociety.org" target="_blank">the Chicago Film Society</a></u>, an organization promoting the preservation of film while also programming screenings at local cinemas.</p>
        <br>
        <p>Perhaps the character of Doc Films is best encapsulated by our entry in Vanity Fair's <i>The Film Snob's Dictionary</i> (March 2004, p. 332): "The film society of the University of Chicago, founded in 1932 as the Documentary Film Group. Hard-core beyond words and lay comprehension, the society is populated by 19-year olds who have already seen every film ever made, and boasts its own Dolby Digital-equipped cinema and an impressive roster of alumni that includes-new Snob-revered critic Dave Kehr."</p>
        <br>
        <p>Doc Films hopes to reach a wide-ranging audience, from film aficionados to casual moviegoers, by cultivating and facilitating an excitement for the study of film. Our mission is to nurture and inspire future writers, filmmakers, and creative artists to tackle the professional world of cinema.</p>
        
