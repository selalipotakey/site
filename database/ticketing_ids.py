# edit your `parent_directory` and `output_name` variables if need be (way down at the bottom)
# run the script and you're done!

def main():

    # the file path to the ticketing_links/ directory on your local machine
    parent_directory = r"/home/cameronpoe/Desktop/docfilms_site_container/site/database/ticketing_links"

    # the file name of the text file you want the links written out to
    # follow the format YYYY_quarter_ticketing_links.txt
    output_name = r"2023_spring_ticketing_links.txt"

    # outputs a .txt file with just the URLs
    simple_links = True

    # outputs a .txt file with the URLs formatted into an <a href=""></a> attribute in HTML (for easy copy-pasting into the webpages)
    website_hrefs = True

    if simple_links:
        f = open(os.path.join(parent_directory, output_name), 'w', encoding='utf-8')

        for item in ticketing_dict["searchResults"]:
            f.write(item[6] + ':\n' + r'https://tickets.uchicago.edu/Online/seatSelect.asp?createBO::WSmap=1&BOparam::WSmap::loadBestAvailable::performance_ids=' + item[0] + "\n\n")

        f.close()
    
    if website_hrefs:
        modified_output_name = 'hrefs_' + output_name 
        f = open(os.path.join(parent_directory, modified_output_name), 'w', encoding='utf-8')

        for item in ticketing_dict["searchResults"]:
            link = r'https://tickets.uchicago.edu/Online/seatSelect.asp?createBO::WSmap=1&BOparam::WSmap::loadBestAvailable::performance_ids=' + item[0]

            href = r'<p><b>Tickets can be bought <u><a href="' + link + r'" target="_blank">here</a></u>.</b></p>'

            f.write(item[6] + ':\n' + href + "\n\n")

        f.close()


